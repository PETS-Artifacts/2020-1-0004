						<p>This policy explains what information is gathered from visitors to the University of Aberdeen's website, and how that information is used. Our website is designed in accordance with the Information Commissioner's Office guidance on the use of cookies and similar technologies<sup><a href="http://www.abdn.ac.uk/about/our-website/privacy.php#_ftnref1">1</a></sup>.</p>

<h2>Information Collected</h2>

<p>Information is gathered from visitors via several methods that are outlined below:</p>

<h3>1. Logfiles</h3>

<p>The University of Aberdeen's central web server [<a href="http://www.abdn.ac.uk/">www.abdn.ac.uk/</a>] tracks requests made of it to centrally held log files. Log files can contain any or all of the following types of data:</p>

<ul>
	<li>The date and time of connection</li>
	<li>Identification of the files requested</li>
	<li>Whether the request was successful</li>
	<li>The size (in bytes) sent in response</li>
	<li>The referring page (where the visitor came from), as supplied by the visitor's browser</li>
	<li>The software used to access the server, as supplied by the visitor's browser</li>
</ul>

<p>Information gathered is used for server system administration, identifying broken links, and for producing usage statistics. The information may also be processed for University academic research projects that have been subject to full ethical and governance approval. Logged information may be kept indefinitely.</p>

<p>The University of Aberdeen also uses the following log reporting services on its website to measure and analyse visitor information:</p>

<ul>
	<li><em>Net Community</em> service from Blackbaud [www.blackbaud.com/netcommunity/]</li>
	<li><em>Analytics</em> service from Google [<a href="http://www.google.com/analytics/">www.google.com/analytics/</a>]</li>
</ul>

<p>Examples of data collected automatically from all users include:</p>

<ul>
	<li>the Internet Protocol (IP) address;</li>
	<li>computer and connection information such as browser type and version, operating system, and platform;</li>
	<li>Uniform Resource Locator (URL) click-stream data, including date and time, and content viewed or searched for on the website.</li>
</ul>

<h3>2. Cookies, web beacons and similar technologies</h3>

<h4>Cookies</h4>

<p>Cookies are small files that are sent from the web server to the visitor's computer via the visitor's browser. They are then stored in the visitor's computer as text files and passed back to the server. Cookies are generally used to identify users and, on occasion, prepare customized web pages for them. Cookies may remain on the visitor's computer merely for the duration of the visit or they may remain indefinitely. Most standard web browsers may be configured to refuse cookies.</p>

<p>The University of Aberdeen uses cookies for the essential running of its websites and web applications. Such cookies are transient, do not contain any personal information, cannot be used to identify you and are exempt from the EU Directive<a href="http://www.abdn.ac.uk/about/our-website/privacy.php#_ftnref2"><sup>2</sup></a>.</p>

<p>The University of Aberdeen also uses cookies for marketing purposes. Use of cookies for these purposes is described in <a href="/about/our-website/cookies.php">our list of cookies</a>. Any other ad hoc use of cookies will be stated separately and explicitly.</p>

<h4>Web Beacons and Similar Technologies</h4>

<p>The University of Aberdeen uses web beacons and similar technologies for marketing purposes. These technologies (also known as "pixel tags", "tracking pixels" or "clear GIFs") retain information in your browser through local objects or storage. Their purpose is to identify users of our site and their behaviour. Information collected may be shared with authorised third-party providers.</p>

<p>Beacons or similar technologies may work in conjunction with cookies and can operate across all of your browsers. In some instances, these may not be fully managed by your browser and may require management directly through your installed applications or device.</p>

<h3>3. Web forms</h3>

<p>Forms on the University of Aberdeen website may request visitors&nbsp;to supply personal details, bank details and/or credit card details in order to receive goods or services, or in order to make donations. Where appropriate, such forms operate over secure connections so that data is safeguarded.</p>

<p>By supplying such information the visitor consents to the University storing the information for a period of time appropriate to the supply of the goods or services requested. Personal information provided will be used for the purpose stated and will not be sold, licensed or traded to third parties. Information entered into a form may be used by the University of Aberdeen to contact you.</p>

<p>Personal information collected and/or processed by the University is held in accordance with the provisions of the <em>Data Protection Act 1998</em>.</p>

<h3>4. Discussion fora, chat rooms, conferencing facilities, bulletin boards and mailing lists</h3>

<p>Tools for synchronous and asynchronous web-based communication that are hosted on the University of Aberdeen websites may request visitors to supply details such as names and email addresses for registration purposes.</p>

<p>By supplying such information the visitor consents to the University storing the information in a manner appropriate for administration of the tool. Information provided will only be used for the purpose stated and will not be sold, licensed or traded to third parties.</p>

<p>Our website may contain profile forms which allow visitors to share&nbsp;personally-identifying information. Visitors&nbsp;may update this information at any time.</p>

<p>Our website may contain real-time communication tools enabling visitors to connect to and communicate with University-authorised individuals.</p>

<p>If visitors supply information via these forms and tools, the University may use the visitor's contact details or&nbsp;information provided&nbsp;to send further information about the organisation to the visitor when necessary. Visitors may opt out of such mailings at any time by contacting the University.</p>

<p>Personal information collected and/or processed by the University is held in accordance with the provisions of the <em>Data Protection Act 1998</em>.</p>

<p><a id="Specific_research" name="Specific_research"></a></p>

<h3>5. Specific research projects</h3>

<p>On occasion, staff involved in certain research projects and clinical trials may wish to gather data from respondents in a more specific manner than those outlined above. In such instances, terms and conditions for the collection and use of information will be stated separately and made explicit to respondents in advance of data collection.</p>

