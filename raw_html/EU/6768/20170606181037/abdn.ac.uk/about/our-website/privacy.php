<h2>Information Collected</h2>

<p>Information is gathered from visitors via several methods that are outlined below:</p>

<h3>1. Logfiles</h3>

<p>The University of Aberdeen's central web server [<a href="http://www.abdn.ac.uk/">www.abdn.ac.uk/</a>] tracks requests made of it to centrally held log files. Log files can contain any or all of the following types of data:</p>

<ul>
	<li>The date and time of connection</li>
	<li>Identification of the files requested</li>
	<li>Whether the request was successful</li>
	<li>The size (in bytes) sent in response</li>
	<li>The referring page (where the visitor came from), as supplied by the visitor's browser</li>
	<li>The software used to access the server, as supplied by the visitor's browser</li>
