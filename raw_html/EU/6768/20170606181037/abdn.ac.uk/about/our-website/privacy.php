<h3>2. Cookies, web beacons and similar technologies</h3>

<h4>Cookies</h4>

<p>Cookies are small files that are sent from the web server to the visitor's computer via the visitor's browser. They are then stored in the visitor's computer as text files and passed back to the server. Cookies are generally used to identify users and, on occasion, prepare customized web pages for them. Cookies may remain on the visitor's computer merely for the duration of the visit or they may remain indefinitely. Most standard web browsers may be configured to refuse cookies.</p>

<p>The University of Aberdeen uses cookies for the essential running of its websites and web applications. Such cookies are transient, do not contain any personal information, cannot be used to identify you and are exempt from the EU Directive<a href="http://www.abdn.ac.uk/about/our-website/privacy.php#_ftnref2"><sup>2</sup></a>.</p>

<p>The University of Aberdeen also uses cookies for marketing purposes. Use of cookies for these purposes is described in <a href="/about/our-website/cookies.php">our list of cookies</a>. Any other ad hoc use of cookies will be stated separately and explicitly.</p>

<h4>Web Beacons and Similar Technologies</h4>

<p>The University of Aberdeen uses web beacons and similar technologies for marketing purposes. These technologies (also known as "pixel tags", "tracking pixels" or "clear GIFs") retain information in your browser through local objects or storage. Their purpose is to identify users of our site and their behaviour. Information collected may be shared with authorised third-party providers.</p>

<p>Beacons or similar technologies may work in conjunction with cookies and can operate across all of your browsers. In some instances, these may not be fully managed by your browser and may require management directly through your installed applications or device.</p>

