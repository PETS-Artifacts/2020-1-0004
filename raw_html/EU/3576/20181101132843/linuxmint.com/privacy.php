<h2 id="cookies-policy">Cookies Policy</h2>
<section>
<div>
<div>
<p><strong>HOW DOES LINUX MINT USE COOKIES?</strong></p>
<p>A cookie is a small piece of text that allows a website to recognize your device and maintain a consistent, cohesive experience throughout multiple sessions. If you use the our websites, both Linux Mint and third parties will use cookies to track and monitor some of your activities, and store and access some data about you, your browsing history, and your usage of the Linux Mint websites.</p>
<p>This policy describes how both Linux Mint and other third parties use cookies and how you can exercise a greater degree of control over cookies. Please keep in mind that this may alter your experience with our platform, and may limit certain features (including being logged in as a user).</p>
<p><strong>Cookie banners</strong></p>
<p>We use cookie banners to warn you about the fact that our websites use cookies. These banners themselves set a cookie when you dismiss them. Without this cookie, the cookie banners could not remember that you dismissed them and would constantly appear every time you load a new web page.</p>
<p><strong>Session cookies</strong></p>
<p>We use session cookies for account registrations and log ins. These cookies are generated and necessary for our web software to authenticate you and log you in your account correctly.</p>
<p><strong>Advertising</strong></p>
<p>We use Google Adsense to provide advertising and help fund our project.</p>
<p>On all websites using Adsense, including Linux Mint's, Google use cookies to track your web activity and to guess what commercial products you're most likely to be interested in. This information is then used by Adsense to provide personalized adverts to you.</p>
<p><strong>Analytics</strong></p>
<p>On URLs which start with linuxmint.com/start, we use Google Analytics to measure the number of visits and have a rough idea of how successful new releases are.</p>
<p>Google Analytics use cookies to provide us with this information.</p>
</div>
</div>
