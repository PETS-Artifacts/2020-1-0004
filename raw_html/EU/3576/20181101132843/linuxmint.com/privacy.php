<p><strong>DATA SECURITY</strong></p>
<p>Security is important to us and we know it is important to you. Linux Mint follows generally accepted standards to protect your personal information when processing, transferring, and storing your personal information including Transport Layer Security (&ldquo;TLS&rdquo;), by restricting your personal data when we do not need to access it and by keeping your personal data only as necessary.</p>
<p>As the data provider, you too have an important role to play in helping us protect your personal information. We encourage you to protect yourself against unauthorized access to your personal information by choosing a password carefully and in accordance with industry best standards. Do not share your password with anyone you do not trust, and make sure your computer accesses Linux Mint via a secure network and that you do not leave your personal information vulnerable to hackers and other bad actors by leaving your computer unattended or by failing to logout of your account when you have ended your session activity.</p>
<p>All records containing personal information are considered to be proprietary and are afforded confidential treatment at all times. Linux Mint is committed to the safety and security of your personal data and the information that you share with us and with the public. We treat your personal safety and safety from harassment as top organizational priorities. At Linux Mint we consider it necessary to pursue these legitimate interests in order to keep our community, you and your information safe from spam, harassment, intellectual property infringement, identity theft, and the scraping and unlawful collection of your information.</p>
