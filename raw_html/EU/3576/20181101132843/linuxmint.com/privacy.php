<p>In accordance with the GDPR, you also have the right to data access and portability (including the right to obtain and download a copy of the personal data you provided to Linux Mint), data correction (the ability to update the personal data we collect and display on you, in many cases via your account settings), data deletion (where Linux Mint will delete personal information stored on you, which we are not required by necessity to maintain), and the ability to have us stop processing your personal data (with certain exceptions as may be necessary). Please also be aware that in order to provide some of these data choices to you, we may need to first verify your identity. We will make reasonable efforts to verify your identity, however if we are unable to do so, we reserve the right to refuse such a request in keeping with data privacy and information security principles.</p>
