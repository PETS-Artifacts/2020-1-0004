<section>
<div>
<div>
<p><strong>COLLECTED DATA<br /></strong></p>
<p>This section details what data is gathered and how it is collected.</p>
<p><strong>Account Registration</strong></p>
<p>Although the websites and products provide for anonymous and pseudonymous participation, in some instances in order to use certain features, we require account registration. This requires a unique nickname associated with your account, an email address, and in some cases, additional information including, an IP address and a password to help secure your personal information.</p>
<p><strong>Profile Information</strong></p>
<p>When setting up a public &ldquo;profile&rdquo; or a signature, you may share information that is not required but will be displayed publicly at your election.</p>
<p><strong>Server and Website Information<br /></strong></p>
<p>Linux Mint uses server software such as Apache, Wordpress, Akismet, PHPBB which receive and record networking information sent by your computer and your browser or mobile device when you visit our websites. This information may include your Internet Protocol (IP) address or unique device identifier. This information is stored in secure logs and is collected automatically.</p>
<p><strong>OAuth</strong></p>
<p>In an effort to reduce the amount of data we gather and to no longer store email addresses and passwords, we offer single sign-on solutions for account login on some of our websites. As a result, third-parties including Github, Facebook and Google may receive information when you elect to use them. When Linux Mint shares your personal information and other collected information with third party service providers, we require that they use your information only for the purpose of providing services to us and that their terms are consistent with this privacy policy.</p>
<p><strong>Google Adsense<br /></strong></p>
<p>Authorized third parties such as Google Adsense place cookies as described in our <a href="#cookies-policy">Cookies Policy</a>. We use this information to serve you certain advertising content.</p>
<p><strong>Google Analytics<br /></strong></p>
<p>Linux Mint uses Google Analytics for pages which URL starts with linuxmint.com/start/. The information collected is anonymized and is used by Linux Mint to have a rough idea of how successful each particular release is.</p>
<p><strong>Paypal</strong></p>
<p>When you make a donation we receive personal information from Paypal. Transaction dates, amounts and county of origin are kept and shared with accountants and the Irish tax authorities. Although your full name and your postal address are kept by Paypal and accessible by us via Paypal, we do not store, copy or share this information.</p>
<p>We keep a record of your email address to be able to identify future donations from being associated with you and to provide you with information and access to the list of donations you sent us.</p>
