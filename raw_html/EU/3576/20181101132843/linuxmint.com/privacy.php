<p>You can adjust the settings on your internet browser and choose from the available Cookies setting to best meet your preferences. While setting options may vary from browser to browser, you can generally choose to reject some or all cookies, or instead to receive a notification when a cookie is being placed on your device. For more information, please refer to the user help information for your browser of choice. Please keep in mind that cookies may be required for certain functionalities, and by blocking these cookies, you may limit your access to certain parts or features of our sites and platforms.</p>

<p>You can opt out of personalised advertising from Google by visiting <a href="https://www.google.com/settings/ads">Ads Settings</a> and <a href="http://www.aboutads.info/choices"/>aboutads.info</a>.</p>

