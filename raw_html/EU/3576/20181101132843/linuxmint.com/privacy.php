<p>The Linux Mint Privacy and Cookie Policy was last updated on May 25, 2018.</p>
<h2>Philosophy</h2>
<p>Your privacy is very important to us. At Linux Mint Team we are committed to gathering as little personal data as possible, ideally and in mosts cases, no data at all, and when data is gathered to protecting and respecting it.</p>
<p><strong>GUIDING PRINCIPLES</strong></p>
<p>Here are our key principles when it comes to privacy:</p>
<ul>
<li>Your data belongs to you.</li>
<li>We respect your rights, in particular your rights to access and modification of your data.</li>
<li>We do not look at personal data as an asset, but as a liability.</li>
<li>We only store personal data when it is needed, either for our services to operate properly, or to comply with the law.</li>
<li>We want as little of your personal data as possible. For most of the services and products we provide, we do not need to know anything personal about you. We try to reduce the amount of data we gather as much as possible.</li>
<li>We are transparent about data collection and use. If anything isn't clear or satisfactory, please contact us and we'll help and/or fix the issue.</li>
<li>We protect data via strong security, encryption and anomyzing techniques.</li>
<li>We do not store contact information for marketing purposes.</li>
</ul>
<h2>Privacy Policy</h2>
<section>
<div>
<div>
<p><strong>INTRODUCTION</strong></p>
<p>What is herein referred to as "products" is a set of Internet websites, products and services operated by Linux Mint Ltd. (&ldquo;Linux Mint&rdquo;, &ldquo;we&rdquo; or &ldquo;us&rdquo;), a company registered in the Republic of Ireland. This privacy policy describes what information we collect, how we use that information, and what options you have to control your personal information.</p>
<p>We sometimes need to process your personal information in order to provide our products. By using our products you confirm that you have read and understood this privacy policy. If you do not want us to collect or process your personal information as described herein, you have several choices including limiting the information we collect on you, or not using our products.</p>
<p>By using these products, you acknowledge that Linux Mint will use your information in the Republic of Ireland and in any country where Linux Mint operates. This privacy policy complies with the EU&rsquo;s General Data Protection Regulation (GDPR). Please be aware that the privacy laws and standards in certain countries, including the rights of authorities to access your personal information, may differ from those that apply in the country in which you reside. We will transfer personal information only to countries to which we are permitted by law to transfer personal information and we will continue to ensure that your personal information is appropriately safeguarded.</p>
</div>
