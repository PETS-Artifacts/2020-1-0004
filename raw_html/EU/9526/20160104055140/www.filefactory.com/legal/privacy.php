		var joinModal = new BootstrapDialog({
			title: 'Upgrade',
			message: $("#joinModal").html(),
			nl2br: false,
			buttons: [{
				label: "Don't show again",
				cssClass: 'btn-link pull-left',
				id: 'notagain',
				action: function(dialog) {
					$.cookie("join-modal", '1', { expires: 30, path: '/' });

					dialog.close();
				}
			},{
				label: 'Close',
				cssClass: 'btn-info',
				action: function(dialog) {
					dialog.close();
				}
			}],
			onshow: function(dialogRef) {
				// Preset the width to match the data-width attribute
				dialogRef.getModalDialog().css('width', $("div#joinModal").attr('data-width'));
				// Get rid of the body padding (so the image takes up the full modal)
				dialogRef.getModalBody().css({'padding' : 0, 'margin-top' : '-1px'});

				resizePromoModal();
			},
			onshown: function(dialogRef) {
				var resizeTimeout;

				$(window).on('resize.modal', function(){
					resizeTimeout && clearTimeout(resizeTimeout);
					resizeTimeout = setTimeout(function(){
						// Fix modal width 
						resizePromoModal();
					}, 10);
				});
			},
			onhide: function(dialogRef) {
				$.cookie("join-modal", '1', { expires: 1, path: '/' });
			}
		});
		
		/**
		 * This hacky function overrides the default resize behavior of
		 * bootstrap-modal - by default the modal will stretch if the
		 * viewport is < 979px, this prevents that and instead ensures
		 * that the modal remains at a fixed with and stays centered.
		 */
		function resizePromoModal() {
			if ($(window).width() <= 1000) {
				if ($(window).width() > $("div#joinModal").attr('data-width')) {
					var cssText = "display: block; left: 50% !important; width: " + $("div#joinModal").attr('data-width') + "px !important; margin-left: -" + (parseInt( $("div#joinModal").attr('data-width'), 10) / 2) + "px !important";
				} else {
					var cssText = "display: block;";
				}

				// cssText is used to allow the !important overrides, because bootstrap-modal uses them by default..
				joinModal.getModalDialog().css('cssText', cssText);
			}
		}
		
		// Show the modal after 10s
		setTimeout(function(){ joinModal.open(); }, 10000);
	});
