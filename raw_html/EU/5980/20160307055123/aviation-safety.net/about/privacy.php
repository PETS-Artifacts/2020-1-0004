<p />
<span class="caption">
We respect your right to privacy and will protect it when you visit our website.
This Privacy Policy explains our online information practices only, including how we collect and use your personal information. It does not apply to third-party websites that you are able to reach from aviation-safety.net. We encourage you to read those privacy policies to learn how they collect and use your information.
</span><br />
<p />
	<span class="caption">What we automatically collect online</span><br />
We collect information about your visit that does not identify you personally. We can tell the computer, browser, and web service you are using. We also know the date, time, and pages you visit. Collecting this information helps us design the site to suit your needs. In the event of a known security or virus threat, we may collect information on the web content you view.

<p />
<span class="caption">Other information we may collect</span><br />
When you visit our website, we may request and collect your contact information. For donations and purchases of subscriptions, we use Paypal to handle all payments. Payment information is supplied to Paypal and is not collected by ASN. Please also see the <a href="https://cms.paypal.com/ie/cgi-bin/marketingweb?cmd=_render-content&content_ID=ua/Privacy_full&locale.x=en_US">Privacy Policy for PayPal Services</a>.

<p />
<span class="caption">Why we collect information</span><br />
Our principal purpose for collecting personal information online is to provide you with what you need and want, address security and virus concerns, and to ease the use of our website.
<p />
We will only use your information for the purposes you intended, to address security or virus threats, or for the purposes required under the law. 
<p />
<span class="caption">We collect information to:</span><br />
<ul>
<li>Reply to your e-mail inquiries</li>
<li>Manage your access to restricted areas of the website</li>
<li>Register you for an e-mail subscription</li>
</ul>
<p />
<span class="caption">Sharing your information</span><br />
We will not share any personal information with third parties.
<p />

<span class="caption">Choices on how we use the information you provide</span><br />
Throughout our website, we will let you know whether the information we ask you to provide is voluntary or required. By providing personally identifiable information, you grant us consent to use this information, but only for the primary reason you are giving it. We will ask you to grant us consent before using your voluntarily provided information for any secondary purposes..

<p /><span class="caption">Cookies or other tracking devices</span><br />
A "cookie" is a small text file stored on your computer that makes it easy for you to move around a website without continually re-entering your name, password, preferences, for example.
<p />
We use cookies on our website to store your username and email address when editing an entry in the Wikibase, but only when you explicitly select the "Remember username and email on this pc"-checkbox.
We also use cookies to store your login details (encrypted) when loggin in to the site as an ASN member, but only when you explicitly select the "remember me"-checkbox.
<p />
Also ASN uses Google Analytics to collect information about your visit anonymously. It reports website trends without identifying individual visitors. Please see the <a href="http://www.google.com/intl/en/analytics/privacyoverview.html">Privacy Statement for Google Analytics</a> which also describes ways to opt-out from this tracking.
<p />
No tracking<br />
If you do not wish to be tracked, the Electronic Frontier Foundation (EFF) offers a browser plugin, <a href="https://www.eff.org/privacybadger">Privacy Bader</a>.

<p /><span class="caption">Social media plugins</span><br />
	Facebook Like Button ("Like")<br />
Facebook plugins are integrated onto the pages of aviation-safety.net from the social network Facebook, 1601 South California Avenue, Palo Alto, CA 94304, USA. You can recognize the Facebook plugins from the Facebook logo or the "Like-Button" ("Like"). You can find an overview of Facebook-plugins by visiting <a href="https://developers.facebook.com/docs/plugins">https://developers.facebook.com/docs/plugins</a>.
When you visit a page with a Facebook plugin, the plugin establishes a direct connection between your browser and the Facebook server. Facebook receives the information that you have visited with your IP address page from aviation-safety.net. If you click on the Facebook "Like" button while you are logged into your Facebook account, you can link to the content on the aviation-safety.net pages from your Facebook profile. Facebook can thereby assign your visit to a aviation-safety.net webpage to your account. Please note that we as provider of the aviation-safety.net have no knowledge of the content of the transmitted data or their use through Facebook. For more information, please see the Facebook privacy policy <a href="https://www.facebook.com/policy.php">https://www.facebook.com/policy.php<a/>
If you do not want Facebook to be able to assign your visit to a page on aviation-safety.net to your Facebook account, then please log out of your Facebook account.
<p />
Twitter<br />
Aviation-safety.net uses embed and retweet functions of the Twitter service. These functions are offered by Twitter Inc., 795 Folsom St., Suite 600, San Francisco, CA 94107, USA. By using the "retweet" function the web pages you visit are linked to your Twitter account and made known to other users. This data is also transmitted to Twitter.
Please note that aviation-safety.net as provider of the webpages has no knowledge of the content of the transmitted data and their use of them by Twitter. For more information, please see the Twitter privacy statement at <a href="https://twitter.com/privacy">https://twitter.com/privacy</a>.
You can change your privacy preferences on Twitter, under the account settings <a href="https://twitter.com/account/settings">https://twitter.com/account/settings</a>.
<p />
Google +<br />
Collection and dissemination of information:
You can publish information worldwide with the Google + button. By using the Google + button you and other users can receive personalised content from Google. Google will store both the information that you have given content +1, as well as information about the page you viewed when you click on +1. Your +1 can be shown as evidence along with your name and profile photo in Google services, such as in search results or in your Google profile, or elsewhere on websites and ads on the internet.
Google records information about your +1 activity to improve Google's services for you and others. To use the Google +1 button, you need a globally visible, public Google profile, which must contain at least the chosen profile name. This name is used in all Google services. In some cases, this name can also substitute any other name that you used when sharing content under your Google Account. The identity of your Google profile may be shown to users who know your email address or other identifying information about you.
Using the information collected:
In addition to the above the information you provide will be used in accordance with the applicable Google privacy policy. Google may release aggregate statistics on the +1 activities of the user or pass these onto users and partners.
