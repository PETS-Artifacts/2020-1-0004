                        <ul>
                            <li><strong>Account</strong> - If you set up an account with us, we will store details of your username and password, your settings preferences (e.g. language, currency and distance units), your past trips and any wish lists that you create.</li>
                            <li><strong>Booking Details</strong> - When you make a booking we will ask you for information that we need in order to process the booking. This will include your name, email address, nationality, gender, mobile number (if you want to receive a free confirmation by SMS message) and your payment details.</li>
                            <li><strong>Queries and Complaints</strong> - If you contact our Customer Service team with a query or a complaint, we will record details of the query or complaint and how it is dealt with on our systems. We may also seek further details about the query or compliant from an accommodation provider, which we will also record in our systems.</li>
                            <li><strong>Reviews</strong> - If you leave a review in relation to your stay at a property, this will be linked to your Hostelworld account, which means we will treat it as containing your personal data.</li>
                            <li><strong>Email Marketing</strong> - When we collect your email address we will record your preferences regarding receipt of marketing emails from us in relation to our own products and services and those of third parties.</li>
                            <li><strong>Competitions</strong> - If you enter into a competition, we will collect details from you in order to enter you into the competition (e.g. your name, email address and your competition entry).</li>
                            <li><strong>Location Data</strong> - If you enable location services for our app and perform a search using your current location or view map directions to the hostel from your location, we will record your location . We also use your IP address to determine what country you are in when you make a booking.</li>
                            <li><strong>Surveys</strong> - If you participate in a survey, we will record your responses to the survey. In some cases we anonymise your responses, but where we don't do this we will let you know.</li>
                        </ul>
