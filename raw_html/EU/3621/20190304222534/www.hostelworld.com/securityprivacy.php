<p>In order for us process your booking we will ask you for certain information, such as your name, your email address, your payment information and booking details.  We also collect certain information from the device you use to access our website, such as your IP address, what browser you use and what type of device you are using.  In addition to the information that we collect ourselves, we sometimes receive information that is collected on our behalf by third parties, such as when you see a Hostelworld advert displayed on a third party site.  For more details about the information that we collect, please <a href="#" class="clickme">click here</a>.</p>

<div class="item-level2">
