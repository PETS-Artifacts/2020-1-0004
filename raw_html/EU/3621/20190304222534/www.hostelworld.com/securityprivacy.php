                <div class="item"><h2>What is the purpose of this notice?</h2>

<p>Hostelworld has been processing online hostel reservations since 1999, and while much has changed in the world of hostel travelling over the years, one thing that hasn't is our commitment to protecting your privacy. This notice sets out details of the information that we collect about you, how we process it, to whom we disclose it, and your rights under data protection law in relation to our processing of your data.</p>

<p>This data protection notice covers personal data that is collected through all of our websites (such as Hostelworld, Hostelbookers and Hostels.com), through our mobile sites and applications, and through booking engines that are integrated into third party websites. Although you should bear in mind that where you access our booking engine on a third-party website, this data protection notice only applies to information we collect through your use of the booking engine, and does not apply to data collected or held by the operator of the third party website.</p></div>
                <div class="item"><h2>Who controls the use of your personal data?</h2>

<p>When this data protection notice mentions "we", "us", "our" or "Hostelworld" we are referring to Hostelworld.com Limited.  This is the Irish company that operates the booking platform that allows you to make reservations, and is in charge of the processing of your personal data.  For more information about us, and contact details for our Data Protection Officer, please <a href="#" class="clickme">click here</a>.</p>

<div class="item-level2">
    <p>Hostelworld.com Limited's registered company number is 337103, and its registered address is 1 Central Park, Leopardstown, Dublin 18.  You can contact us using the details at the end of this notice, or if you have any queries in relation to the processing of your personal data you can also contact our Data Protection Officer by emailing <a href="mailto:DataProtectionOfficer@hostelworld.com">DataProtectionOfficer@hostelworld.com</a> or by calling +353 1 498 0700.</p>
</div></div>
