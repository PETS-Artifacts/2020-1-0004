<p>				Hostelworld.com Limited ("<strong>Hostelworld</strong>") operates the Hostelworld.com website, mobile site,  mobile app and a booking engine, which is integrated into third party websites  (together the "<strong>Hostelworld Sites</strong>"). This privacy statement sets out our privacy  and security practices and forms part of the Terms and Conditions that apply to  your use of the Hostelworld Sites. Please  note that where you access our booking engine on a third party website this  privacy statement only applies to information we collect through your use of  the booking engine, and does not data collected or held by the operator of the  third party website.</p>

<p>				Hostelworld has been processing online hostel reservations  since 1999 and we have always placed the security of our internet transactions  and our customers" credit card details as our top priority. We will continue to offer our customers the  highest level of security available for online transactions and hope this  reassures you when you make your reservations with us.</p>

<p>				Hostelworld realises that some people are unsure about  entering their personal details into websites and apps over the Internet. Below,  we explain how we use your information and how we protect your privacy.  Hostelworld and its associated companies respect and protect your right to  privacy in relation to your interactions with the Hostelworld Sites. Any  information which is provided by you to Hostelworld via the Hostelworld Sites  or otherwise will be treated in accordance with the terms of the Irish Data  Protection Acts 1988 and 2003 and/or such amending or replacement legislation  as may be adopted in Ireland from time to time.</p>
				
				<h3>INFORMATION  REQUIRED TO PROCESS YOUR BOOKING</h3>
<p>				For us to process a booking for you on behalf of a travel  services provider, we require some information from you. This includes your  name, nationality, your gender (or the gender of the group you are in), your  phone number and email address. Also, since we confirm your booking while you  are on the Hostelworld Site, we require your credit or debit card details so we  can guarantee your beds when you pay your deposit.</p>
				
				<h3>WHO SEES YOUR  INFORMATION</h3>
<p>				We are required to send your information to the travel  services provider with which you booked travel services so that the travel  service provider knows who you are and when you will use its travel services.  We give your name, your nationality, your gender (or the gender of the group you  are in), your phone number, your email address, your credit card details and of  course, the details of your booking. The travel service provider requires this  information to accept a confirmed booking from you. All travel service  providers on whose behalf we facilitate bookings have agreed to the Hostelworld  terms and conditions and accept this privacy policy.</p>

<p>				We may also pass your personal information to third party  service providers who process it on our behalf in order to allow us to provide  the best service to you. For example, we  use various technologies provided by Google, including its Maps product and its  analytics products, to enhance the Hostelworld Sites. The following websites set out Google"s  privacy policies in connection with these services <a href="http://www.google.com/policies/privacy/partners/"  target="_blank">http://www.google.com/policies/privacy/partners/</a> and <a href="http://www.google.com/policies/privacy/"  target="_blank">http://www.google.com/policies/privacy/</a>.  </p>
<p>
We only pass your personal information to trusted third party services  providers in accordance with our obligations under the Irish Data Protection  Acts 1988 and 2003.</p>
				
				
				<h3>SECURITY</h3>
<p>				All personal information exchanged between your device and  the Hostelworld Sites is encrypted using TLS. Our TLS Cert. is provided by GlobalSign.  This ensures that your information is transmitted securely and is safe from  third parties. </p>

<p>				You should be aware that where you link to another website  from the Hostelworld Sites, Hostelworld has no control over that other website.  Accordingly, Hostelworld cannot guarantee that the controller of that website  will respect your privacy in the same manner as Hostelworld.</p>
				
				
				
				<h3>HOW WE USE YOUR  INFORMATION</h3>
<p>				In addition to the use and disclosure of information which  is required when you avail of our travel related and data processing services  (and which we describe above), we use your email address for sending you a  confirmation of your bookings, pre-trip reminder e-mails, an email reminder to  provide a guest review of your last accommodation stay, or occasionally to  inform you of new features on the Hostelworld Sites. Our booking confirmation  and pre-trip reminder emails may contain banner advertising. We only use your  phone number to contact you urgently if there is a problem with your booking.  In circumstances where you contact us with a query, we use your contact details  in order to respond to your query. </p>

<p>				Where you have given us your consent, we may send you  emails that we think will be useful to you.  This may include our regular e-newsletter, special offers, travel tips,  customer surveys and information about products and services offered by us or  our selected partners. Where your  settings on your device permit us to send app notifications we may also send  you app notifications containing similar information. You may opt out of receiving email messages  at any time by clicking on the link provided in the email messages, and app  notifications by changing the notification settings in your app. </p>
				
<p>				We take reasonable steps to ensure that your information  is relevant to its intended use, and that it is accurate and up to date. As a registered Hostelworld.com user, you can  access and update your contact information, personal preferences and e-mail  subscription choices at any time by visiting the account section of the Hostelworld.com  website. The information that you submit helps us to make your emails more  useful, interesting and relevant to you.</p>
				
<p>				We use cookies and pixel tags on the Hostelworld Sites and  in our e-mails in accordance with our Cookies Policy, which is available <a href="/cookiespolicy.php" target="_blank">here</a>. </p>

<p>				On occasions, we may supply statistics about our customers  to third-parties such as hostels or other agents. These statistics never  contain personally identifying information.</p>
				
<p>				We would not sell or rent your personal information to  third parties for their marketing purposes without your prior consent. </p>
				
				
				
