				<p>We want to make it easy for you to take advantage of the  travel related opportunities which we feature on the Hostelworld Sites. We work  with our advertising partners in order to try and ensure that when you see an  advert for a Hostelworld brand it will be relevant to you and your interests.  This applies to adverts that are displayed on Hostelworld"s own websites  (including the Hostelworld Sites, in Hostelworld emails, and on websites owned  by third parties). We do this by allowing our third party advertisers to  collect non-personally identifiable information about your visit to the Hostelworld  Sites, such as the pages you visit on the Hostelworld Sites and the locations  you appear to be interested in. We also provide third party advertisers with  non-personally identifiable information that we collect about you when you use  the Hostelworld Sites in order to only show you relevant adverts. We may also  use such information in order to avoid showing you adverts on third party  websites like Facebook and Twitter if we don"t think that you will be  interested in them.</p>
				
<p>				When we share your information with third party advertisers  we always ensure that we do so in compliance with our obligations under data  protection law. We may use technologies such as "hashing" to ensure that we do  not provide our third party service providers with your personal data. For more  information on how our third party advertising service providers gather  information about your use of the Hostelworld Sites, please refer to our  Cookies Policy which is available <a href="/cookiespolicy.php" target="_blank">here</a>.</p>
				
