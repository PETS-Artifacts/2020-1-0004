האתרים משתמשים ב'עוגיות' (Cookies) לצורך תפעולם השוטף והתקין, ובכלל זה כדי לאסוף נתונים סטטיסטיים אודות השימוש באתרים, לאימות פרטים, כדי להתאים את האתרים להעדפותיך האישיות ולצרכי אבטחת מידע. 'עוגיות' (Cookies) הן קבצי טקסט, שהדפדפן שלך יוצר לפי פקודה ממחשבי OKGOALS. חלק מהעוגיות יפקעו כאשר תסגור את הדפדפן ואחרות נשמרות על גבי הכונן הקשיח במחשב שלך. אם, לדוגמה, אתה משתמש במערכת ההפעלה "חלונות" ובדפדפן מסוג אינטרנט אקספלורר של חב' מייקרוסופט, תוכל למצוא אותם בספריה c:windows.cookies וכן ב- c:windows.Temporary Internet Files. ה-Cookies מכילים מידע מגוון כדוגמת הדפים שבהם ביקרת, משך הזמן ששהית באתרים, מהיכן הגעת אל האתרים, מדורים ומידע שאתה מבקש לראות בעת הכניסה לאתרים ועוד. הם משמשים גם כדי לייתר את הצורך בהזנת פרטיך בכל פעם שאתה מבקר מחדש במדורים באתרי OKGOALS המחייבים רישום. המידע ב- Cookies מוצפן, וידיעות אינטרנט נוקטת צעדי זהירות כדי להבטיח שמחשבי OKGOALS יוכלו לקרוא ולהבין את המידע האגור בהם.

 

אם אינך רוצה לקבל Cookies, תוכל להימנע מכך על ידי שינוי ההגדרות בדפדפן שלך. לשם כך נא היוועץ בקובץ העזרה של הדפדפן. זכור, עם זאת, שנטרול העוגיות עלול לגרום לכך שלא תוכל להשתמש בחלק מהשירותים והתכונות באתרי OKGOALS או באתרי אינטרנט אחרים. בנוסף לכך, אתה יכול למחוק את ה- Cookies במחשבך בכל רגע. מוצע שתעשה כן, רק אם אתה משוכנע שאינך רוצה שאתרי OKGOALS יותאמו להעדפותיך. הואיל והעוגיות מונעות ממך לעתים את הצורך להזין שמות משתמש וסיסמאות, אל תמחק אותן אלא אם כן אתה משוכנע שרשמת תחילה את כל הפרטים הדרושים לשימוש באתרים במקום בטוח.
 		שירותים של צדדים שלישיים		
חלק מהשירותים באתרי OKGOALS, לדוגמה מנוע חיפוש הכתבות וערכי האנציקלופדיה, מנוהלים על ידי שותפי מסחר ותוכן של ידיעות אינטרנט. שירותים אלה מוגשים באמצעות מחשביהם של אותם שותפים ולא ממחשבי ידיעות אינטרנט. במהלך השימוש שלך בשירותים אלה יתכן ותתבקש למסור פרטים אישיים, או שייאסף לגביך מידע. השימוש במידע זה כפוף למדיניות הפרטיות של מנהלי השירותים הללו ולא למדיניות הפרטיות של OKGOALS ולכן מומלץ שתעיין גם במסמכי מדיניות הפרטיות שלהם.
 		פרסומות של צדדים שלישיים		
ידיעות אינטרנט מתירה לחברות אחדות לנהל את מערך הגשת הפרסומות באתרי OKGOALS ו/או משתמשת במערכות מטעם חברות אחרות, לדוגמה חברת טוטאל מדיה, בקשר לניהול מערך הגשת הפרסומות באתרי OKGOALS. המודעות, שבהן אתה צופה בעת הביקור באתרי OKGOALS, מגיעות ממחשביהן של אותן חברות. לצורך ניהול הפרסומות, חברות אלה מציבות Cookies במחשבך ומשבצות "משואות רשת" (web beacons) במודעות הפרסומת, או בדפי האתר. המשואות הן קבצים גראפיים זעירים בעלי מזהה ייחודי, המשובצים בדפי אינטרנט ושתפקידם לסייע באיסוף מידע אודות הצפייה והשימוש באתר וכן נתוני קהל (כגון גיל, מין ותחומי עניין) המופקים מפרסום מבוסס עניין של Google או מפרסמים אחרים באמצעות קוד Google Analytics. המידע הנאסף איננו מזהה אותך, אלא רק מבקש להתאים את הפרסומות שיוצגו בפניך לנושאים שיעניינו אותך. השימוש שחברות אלה עושות ב-Cookies ובמשואות רשת כפוף למדיניות הפרטיות שלהן ולא למדיניות זו של אתרי OKGOALS. אם אתה רוצה לבדוק את מדיניות הפרטיות של החברות, המנהלות את מערך הפרסום ב-OKGOALS, תוכל לעשות כן באמצעות אתרי האינטרנט שלהן. לדוגמה, לגבי מערך הגשת הפרסומות של חברת טוטאל מדיה ניתן לקבל מידע בקישור הזה ולגבי זה של Google בקישור זה.
 		איסוף מידע לצרכים סטטיסטיים 	

ידיעות אינטרנט נעזרת בחברות שונות המספקות לה ניתוחים סטטיסטיים אודות השימוש באתרי OKGOALS. החברות אוספות ומנתחות מידע על היקף השימוש באתר, תדירות השימוש בו, מקורות הגישה של המשתמשים לאתר וכיוצא בזה. המידע הנאסף הוא סטטיסטי במהותו, הוא איננו מזהה אותך אישית והוא נועד לצרכי ניתוח, מחקר ובקרה.
 		אבטחת מידע		
ידיעות אינטרנט מיישמת באתרי OKGOALS מערכות ונהלים לאבטחת מידע. בעוד שמערכות ונהלים אלה מצמצמים את הסיכונים לחדירה בלתי-מורשית למחשבי OKGOALS, אין בהם בטחון מוחלט. לכן, ידיעות אינטרנט לא מתחייבת שהשירותים באתרי OKGOALS יהיו חסינים באופן מוחלט מפני גישה בלתי-מורשית למידע המאוחסן בהם.
 		זכות לעיין במידע		

על-פי חוק הגנת הפרטיות, התשמ"א-1981, כל אדם זכאי לעיין בעצמו, או על ידי בא-כוחו שהרשהו בכתב או על ידי אפוטרופוסו, במידע שעליו המוחזק במאגר מידע. אדם שעיין במידע שעליו ומצא כי אינו נכון, שלם, ברור או מעודכן, רשאי לפנות לבעל מאגר המידע בבקשה לתקן את המידע או למוחקו. אם בעל המאגר סירב למלא בקשה זו, עליו להודיע על כך למבקש באופן ובדרך שנקבעו בתקנות. על סירובו של בעל מאגר מידע לאפשר עיון ועל הודעת סירוב לתקן או למחוק מידע, רשאי מבקש המידע לערער לפני בית משפט השלום באופן ובדרך שנקבעו בתקנות.

 

בנוסף, אם המידע שבמאגרי ידיעות אינטרנט משמש לצורך פניה אישית אליך, בהתבסס על השתייכותך לקבוצת אוכלוסין, שנקבעה על פי איפיון אחד או יותר של בני אדם ששמותיהם כלולים במאגר ("פניה בהצעה מסחרית"), כי אז אתה זכאי על-פי חוק הגנת הפרטיות, התשמ"א-1981 לדרוש בכתב שהמידע המתייחס אליך יימחק ממאגר המידע. ידיעות אינטרנט תימחק במקרה זה מידע הדרוש לה רק כדי לפנות אליך בהצעות מסחריות כאמור לעיל. מידע הדרוש לידיעות אינטרנט לשם ניהול עסקיה - לרבות תיעוד פעולות מסחריות ואחרות שביצעת באתרי OKGOALS - יוסיף להישמר בידיעות אינטרנט על-פי דין, אך לא ישמש עוד לצורך פניות אליך. אם בתוך 30 יום לא תקבל הודעה כי המידע שנתבקשה ידיעות אינטרנט למחוק אכן נמחק על-פי סעיף זה, תהיה זכאי לפנות לבית משפט השלום באופן הקבוע בתקנות שמכוח החוק, כדי שיורה לידיעות אינטרנט לפעול כאמור.
