<h1>Postgraduate Applications Centre - Privacy Statement</h1>
<p>PAC is an organisation which was founded in 1998 to process applications 
centrally for admission to taught &amp; research based postgraduate courses in 
third level institutions in the Republic of Ireland. </p>
<p>
The purpose of the PAC is to process postgraduate applications for higher 
education courses centrally and to deal with them in an efficient and fair manner. </p>
<p>
The HEIs retain the function of making decisions on admissions.</p>
<p>
This statement relates to the privacy practices in connection with the
website of the Postgraduate Applications Centre. Links to other websites are 
clearly identifiable as such. We are not responsible for the content or 
privacy practices of other websites. </p>

<h3>General statement</h3>
<p>The Postgraduate Applications Centre respects your right to privacy, and will not collect any personal information about you on this website without your permission. Any personal information which you volunteer will be treated with the highest standards of security and confidentiality, strictly in accordance with the Data Protection Acts, 1988 & 2003 and the EU <b>General Data Protection Regulation (GDPR)</b></p>
<p>
The Postgraduate Applications Centre will make no attempt to identify individual visitors, or to associate the technical details listed above with any individual. It is the policy of the Postgraduate Applications Centre never to disclose such technical information in respect of individual website visitors to any third party unless obliged to disclose such information by a rule of law. </p>


<h2>Your rights under GDPR</h2>
<p>The European Union’s General Data Protection Regulation (GDPR) protects European Union data subjects' fundamental right to privacy and the protection of personal data. It introduces robust requirements that raise and harmonize standards for data protection, security, and compliance.  PAC processes data from subjects both within the EU and outside it.  While GDPR may not apply to applicants, who reside outside of this jurisdiction PAC never the less will endeavour to provide the same level of security and compliance regardless of your location.  The follow are your rights under GDPR</p>

<h3>Who is Collecting the Data?</h3>
<p>The Joint Controller for the data provided by you is the Postgraduate Applications Centre (PAC) and any Higher Education Institution (HEI) whose course you wish to apply to.
PAC has appointed a Data Protection Officer (DPO). He can be contacted directly via the <a href="&#109;&#x61;&#105;&#108;&#x74;&#x6f;&#58;&#100;&#x70;&#111;&#x40;&#112;&#97;&#99;&#46;&#105;&#x65;">data protection officer email</a> and/or by post. The DPO will provide you with the contact details of the DPO, where applicable, of a HEI.  
</p>
<p>
Further contact details can found on <a href='http://www.pac.ie/contact.php'>our contact page</a>
</p>  

<h3>What data is being collected?</h3>
<p>Registration on the PAC website and/or applying to a course may require the submission of personal data such as, but not limited to: Name, Addresses, Phone Numbers, Email, PPSN, Date of Birth, Higher Education Student Numbers etc.</p>
<p>Completing an application may require uploading or otherwise sending documents containing personal information such as, but limited to: Birth Certificates, Passports, Profile Photographs, Examination Results etc. </p>
<p>The application process may also require personal information about related third parties such referees.   </p>

<h4>Collection and use of technical information</h4>
<p>This website may use cookies.  Visitors can use this website with no loss of functionality if cookies are disabled from the web browser. Technical details in connection with visits to this website may be logged for statistical purposes. The technical details logged are confined to the following items:</p>
<ol>
<li>The IP address of the visitor’s web server</li>
<li>The top-level domain name used (for example .ie, .com, .org, .net)</li>
<li>The previous website address from which the visitor reached us, including any search terms used</li>
<li>Clickstream data which shows the traffic of visitors around this web site (for example pages accessed and documents downloaded)</li>
<li>The type of web browser used by the website visitor.</li>
</ol>

<p>PAC also make use of Google Tag Manager to send anonymous usage statistics to HEIs, about the pages that you visit and the actions you perform.  This information is used for statistical and marketing purposes but is not personal in nature and does not impinge on your rights under GDPR.</p>

<h3>What is the legal basis for processing the data and how will it be used?</h3>
<p>Your data will be processed for the purpose of assessing applications to the HEI.  Once you are registered on the PAC site then the PAC will retain your information whether or not you apply for a course. Your data will also be accessible to HEIs that you apply to. </p>

<h3>Where is data being stored and processed?</h3>
<p>The processing of your data will take place in the offices of the PAC, and an Irish data centre where the PAC is a tenant, and any HEI that you apply to.</p>

<h3>Will the data be shared with any third parties?</h3>
<p>The PAC will transfer your personal data to other organisations. These transfers are normally required for the purpose of making payments through a third party payment provider. The exact nature and purpose of these transfers will be listed on the PAC website. </p>

<h3>How long will the data be stored for?</h3>
<p>Once registered on the site your data will be normally stored for up to 3 years.  This duration is chosen to allow for an applicant to apply for, and complete, a standard length postgraduate course, and subsequently apply for another without the need to re-register on the site which could normally hinder or cause delay to subsequent application processing and management.  In the case that you apply to a course then the PAC will keep the data for up to 3 years from the last date of either application, acceptance of offer, or other decision action such as withdrawal or deferral.  Please note that these actions may be requested by you or by the HEI on your behalf with your agreement.  In the case of an applicant applying to the Professional Master of Education (PME), personal will be kept indefinitely.  This is done to allow for scrutiny or legal challenges to an applicants eligibility as per the requirements set down by the teaching council.  In the case of technical information such data will also be stored for 3 years.</p>

<h3>What rights does the data subject have?</h3>
<p>The following are your rights under the regulations</p>
<ol>
<li>Right of access – you have the right to request a copy of the information that we hold about you in accordance with Section 86 of the Data Protection Act 2018;
</li><li>Right of rectification – you have a right to correct data that we hold about you that is inaccurate or incomplete in accordance with Section 87 and Section 89 of the Data Protection Act 2018;
</li><li>Right to erasure – in certain circumstances, you can ask for the data we hold about you to be erased from our records in accordance with Section 87 and Section 89 of the Data Protection Act 2018;
</li><li>Right to restriction of processing – where certain conditions apply to have a right to restrict the processing in accordance with Section 87 and Section 89 of the Data Protection Act 2018
</li><li>Right of portability – subject to certain restrictions, you have the right to have the data we hold about you transferred to another organisation where we hold it in electronic form. This right of data portability applies to: (i) personal data that we process automatically (i.e. without any human intervention); (ii) personal data provided by you; and (iii) personal data that we process based on your consent or in order to fulfil a contract.
</li><li>Right to object – you have the right to object to certain types of processing such as direct marketing.
</li></ol>

<h3>How do I raise a request or a complaint?</h3>
<p>Requests can be directed to the PAC contact points listed in this Statement.  They will be forwarded on should there be a third party involved in the processing of your personal data.  It should be noted that a request for data access, rectification, erasure, restricted processing, portability etc. may be subject to administration processing fee or 2 Euro.</p>

<h3>Glossary of technical terms used</h3>

<i>web browser</i>
<br />
The software you use to read web pages. Examples include Microsoft Internet Explorer,
Mozilla Firefox, Netscape Navigator, and Opera.
<br /><br />
<i>IP address</i>
<br />
The identifying details for your computer (or your internet service provider’s 
computer), expressed in "internet protocol" code (for example 192.1.214.10). Every 
computer connected to the web has a unique IP address, although the address may not
be the same every time a connection is made.
<br /><br />
<i>cookies</i>
<br />
Small pieces of information, stored in simple text files, placed on your computer
by a web site. Cookies can be read by the web site on your subsequent visits. The 
information stored in a cookie may relate to your browsing habits on the web page, 
or a unique identification number so that the web site can "remember" you on your 
return visit. Generally speaking, cookies do not contain personal information from 
which you can be identified, unless you have furnished such information to the web 
site. 
