<h1>Postgraduate Applications Centre - Privacy Statement</h1>
PAC is an organisation which was founded in 1998 to process applications 
centrally for admission to taught &amp; research based postgraduate courses in 
third level institutions in the Republic of Ireland. 
<br /><br />
The purpose of the PAC is to process postgraduate applications for higher 
education courses centrally and to deal with them in an efficient and fair manner. 
<br /><br />
The HEIs retain the function of making decisions on admissions.
<br /><br />
This statement relates to the privacy practices in connection with the
website of the Postgraduate Applications Centre. Links to other websites are 
clearly identifiable as such. We are not responsible for the content or 
privacy practices of other websites. 
<br /><br />
<h3>General statement</h3>
The Postgraduate Applications Centre respects your right to privacy, and will
not collect any personal information about you on this website without your
permission. Any personal information which you volunteer will be treated 
with the highest standards of security and confidentiality, strictly in 
accordance with the Data Protection Acts, 1988 &amp; 2003.
<br /><br />
<h3>Collection and use of personal information</h3>
The Postgraduate Applications Centre does not collect any personal data about
you on this website, apart from information which you volunteer (for example
by e-mailing us or by using our online application services). Any information
which you provide in this way is made available only to the participating
Higher Education Institutions and is not made available to any other third
parties. The Information you provide is used only in line with the purpose 
for which you provided it.
<br />
<!--06.12.12-->
Please note that the HEIs to which you apply may wish to
communicate with you about your application, and they
may contact you by email, post or SMS text.
<!-- end 06.12.12-->
<br /><br />
You have a right to be given a copy of any of your personal data which
is retained by the PAC. You may submit a request in writing to the above
postal or email address. For identification purposes you must supply your 
full name as you supplied it to PAC, your date of birth, and your postal
address (your PAC application number would be of assistance also). PAC
will provide you with a printed copy of your personal data from our files
within 40 days of receiving your written request. PAC reserves the right 
to charge a fee not exceeding €6.35 for this service, however, given the
small demand for such services to date, personal data printouts are
usually provided by PAC free of charge.
<br />
<!--06.12.12-->
Paper applications and supporting application documents
are retained for two years and then confidentially
destroyed. Electronic data is retained for a period of three
years. No documents will be returned to Applicants.
<!-- end 06.12.12-->
<br /><br />
<h3>Collection and use of technical information</h3>
This website does not use cookies, apart from temporary "session" cookies
which enable a visitor’s web browser to remember which pages on this website
have already been visited. Visitors can use this website with no loss
of functionality if cookies are disabled from the web browser.

Technical details in connection with visits to this website may be logged
for statistical purposes. The technical details logged are confined to the
following items:
<ol>
<li>the IP address of the visitor’s web server</li> 
<li>the top-level domain name used (for example .ie, .com, .org, .net)</li>
<li>the previous website address from which the visitor reached us, including any search terms used</li>
<li>clickstream data which shows the traffic of visitors around this web site (for example pages accessed and documents downloaded) </li>
<li>the type of web browser used by the website visitor.</li>
</ol>
<br /><br />
The Postgraduate Applications Centre will make no attempt to identify individual 
visitors, or to associate the technical details listed above with any individual.
It is the policy of the Postgraduate Applications Centre never to disclose such 
technical information in respect of individual website visitors to any third 
party unless obliged to disclose such information by a rule of law. The technical
information will be used only by the Postgraduate Applications Centre, and only for 
statistical and other administrative purposes. You should note that technical 
details, which we cannot associate with any identifiable individual, do not 
constitute "personal data" for the purposes of the Data Protection Acts, 1988 &amp; 2003.
<br /><br />
<h3>Glossary of technical terms used</h3>

<i>web browser</i>
<br />
The software you use to read web pages. Examples include Microsoft Internet Explorer,
Mozilla Firefox, Netscape Navigator, and Opera.
<br /><br />
<i>IP address</i>
<br />
The identifying details for your computer (or your internet service provider’s 
computer), expressed in "internet protocol" code (for example 192.1.214.10). Every 
computer connected to the web has a unique IP address, although the address may not
be the same every time a connection is made.
<br /><br />
<i>cookies</i>
<br />
Small pieces of information, stored in simple text files, placed on your computer
by a web site. Cookies can be read by the web site on your subsequent visits. The 
information stored in a cookie may relate to your browsing habits on the web page, 
or a unique identification number so that the web site can "remember" you on your 
return visit. Generally speaking, cookies do not contain personal information from 
which you can be identified, unless you have furnished such information to the web 
site. 
