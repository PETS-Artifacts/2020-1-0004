<script type="text/javascript">
var thisScreen,
	infoBarTimeout,
	headerOffset = 30;

function hideInfoBar(){
	$('.info-bar-cap .toggle').removeClass('close-bar'); 

	var header = $('.blue-bar-container').parents('.container-fluid').eq(0),
		top = header.outerHeight()-$('.info-bar-holder').outerHeight()-10;


	$('.info-bar-holder').stop().animate({
		'top' : top+'px'
	});
}

function showInfoBar(){
	$('.info-bar-cap .toggle').addClass('close-bar'); 

	var header = $('.blue-bar-container').parents('.container-fluid').eq(0),
		top = header.outerHeight()-headerOffset;
	$('.info-bar-holder').stop().animate({
		'top' : top+'px'
	});
}

function sizeInfoBar(){

	$('.info-bar-main img').css({
		'max-height' : 'auto',
		'width' : 'auto'
	});

	$('.info-bar-main').css({
		'max-height' : ( $(window).outerHeight()-$('.blue-bar-container').outerHeight()-$('.foorer-wrap').outerHeight()-150 )+'px'
	});

	$('.info-bar-slider').css({
		'height' : 'auto'
	});

	if(thisScreen!='xs'){
		$('.info-bar-slider').css({
			// 'max-height' : (parseInt($('.info-bar-main').outerHeight() -40 ))+'px'
			//'height' : (parseInt($('.info-bar-main').css('max-height') ) -40)+'px'
		});

		$('.info-bar-main img').css({
			'max-height' : ($('.info-bar-slider').outerHeight())+'px',
			'width' : 'auto',
			'max-width' : '100%'
		});

	}
	else{
		$('.info-bar-main img').css({
			'width' : 'auto',
			'height' : 'auto',
			'max-width' : '100%'
		});
	}
}

function adjustScreenType(){
  var screens = $('#screen-type').find('.screen-type');

  screens.each(function(){
    if($(this).css('display') == 'block'){
      thisScreen = $(this).attr('screen');
    }
  });
}

$(document).ready(function(){

	var infobarOwl = $('.info-bar-slider');

	infobarOwl.owlCarousel({
		autoPlay : 4000,
		stopOnHover : true,
		items : 1,
        itemsDesktop : [1199,1],
	    itemsDesktopSmall : [980,1],
	    itemsTablet: [768,1],
	    itemsTabletSmall: false,
	    itemsMobile : [479,1],
	    pagination : false
	});


	var numSlideImages = $('.info-bar-slider img').size(),
		numSlideImagesLoaded = 0;


	//make sure all info-bar images are loaded before sizing
	$('.info-bar-slider img').each(function(){ 
		$(this).one("load", function() {
			numSlideImagesLoaded ++;

			if(numSlideImagesLoaded >= numSlideImages){
				adjustScreenType();
				sizeInfoBar(); 
				if($('body').hasClass('home')){ showInfoBar(); }
				else{ hideInfoBar(); }
			}

		}).each(function() {
		  if(this.complete) $(this).load();
		});
	});



	// setTimeout(function(){ 
		
	// }, 500);

	
	

	$('.info-bar-cap .toggle').click(function(){
		if($(this).hasClass('close-bar')){ hideInfoBar(); }
		else{ showInfoBar(); }
	})

	$('.submenu-link').click(function(){
		$('.submenu-container').slideUp();
		$(this).parents('li').eq(0).find('.submenu-container').stop().slideToggle();
	});

	$(window).resize(function(){
		clearTimeout(infoBarTimeout);
		infoBarTimeout = setTimeout(function(){
			sizeInfoBar();
			hideInfoBar();
		}, 200);

		adjustScreenType();
	});
});

$(document).load(function(){
	adjustScreenType();
});
