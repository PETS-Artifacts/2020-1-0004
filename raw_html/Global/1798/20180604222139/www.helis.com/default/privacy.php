<h2>1. Information We Collect</h2> 

<b>1.1 Information you provide</b>: When you <a href=/database/main/register.php>register for an account</a>,  
we ask for your contact information, including items such as a name, email and your city and country of residence
<br><br>

<b>1.2 Information we collect when you use our services:</b> For each visitor to our website we gather  information automatically 
which may include the internet protocol (IP) address, browser type, internet service provider (ISP), 
referring/exit pages, operating system, date/time stamp, clickstream data, landing page, and referring URL. 
This is done by automated means using cookies, browser analysis tools and server logs.
<br><br>

Our servers are located in the Americas, and all information we hold is stored there. This includes: <br>
- the code to implement and execute our services <br>
- information we use to identify you as a registered or casual user of our sites and database <br>
- content of our sites and database, which we provide <br>
- content of our database to which you can contribute, when you become a registered user <br>
- notifications to us that you have added something to the database in the What’s New section. <br>
<br>
We keep this information about you or what you have contributed until you inform us otherwise (see Section 6).  
Should you choose to delete your account, your contributions to the database will not be erased 
 but we will remove the tags which identify that you have contributed it, except in images where watermarks cannot be removed.
<br><br>

<h2>2. Cookies</h2>

To enable us to manage some of your personal information, we use <i>cookies</i> to help us.
<br><br>

<h3>2.1 What are cookies?</h3>  

Cookies are small text files that are installed on your browser, on your computer or on your mobile device. 
They allow our websites to recognize your device. Cookies can have several purposes: 
to ensure that the website takes into account the user’s preferences by for example remembering 
passwords and log-in details, to collect statistical data, to adapt the content etc.
<br><br>

<h3>2.2 Which cookies do we use?</h3>

We use both first and third party cookies, session-only and persistent cookies:
<br>
- First-party cookies are those set by us on our websites. <br>
- Third-party cookies are set by a different domain than the one of the website being visited (e.g. Google Analytics cookies). <br>
Persistent cookies remain on a user’s device for a certain period (e.g. 1 day, 1 month, 1 year, … ).
<br><br>

<b>2.2.1 Analytical and tracking cookies</b><br>
We use (mainly) third party cookies from companies such as Google Analytics, Bing etc. 
to understand how users arrive on our websites and how they interact with us on the website.
We also use these cookies to understand how our media campaigns work. This information is used 
to evaluate and improve the content of our website and our campaigns and ultimately to improve user experience.
<br><br>

We also use these analytics stats to assess how successfully (or otherwise) our users find what they are looking for 
on our sites. We use this third party service so that we can concentrate on our sites’ subject
and they can tell us how effective our site search is. 
We can sometimes use that information to improve how you can find information on our sites.
<br><br>

<b>2.2.2 Functional cookies</b><br>
We use functional cookies to allow a site to remember choices you make  
and provide more enhanced, personal features. 
These cookies cannot track your browsing activity on other websites 
and they do not gather any information about you that could be used for advertising or 
remembering where you’ve been on the Internet outside our sites.
<br><br>

<b>2.2.3 Security cookies</b><br>
We use security cookies to ensure the security of our website. For example when a registered user logged in.
<br><br>

<b>2.2.4 Social sharing cookies</b><br>
We use social sharing cookies from LinkedIn, Twitter, Google, Facebook, YouTube, Tumblr, Instagram, Vimeo 
to enable you to share any content of interest on social networks 
and sometimes we may present some targeted advertisements on these networks based on your interaction with our websites.
<br><br>

<b>2.2.5 Advertising cookies</b><br>
We use second and third party advertisers for all of the advertising that appears on our websites.
Our websites are free to join and use and doing this allows us to maintain and continue our work online.
<br><br>

- Google, as a third party vendor, uses the DART cookie to serve ads.
    You  may opt out of the use of the DART cookie by visiting the 
<a href=http://www.google.com/privacy_ads.html>Google ad and content network privacy 
policy</a>. 
<br> 

- To learn more about this behavioral advertising practice or to opt-out of this type 
of advertising, you can visit <a href=http://www.networkadvertising.org>www.networkadvertising.org</a>
<br><br>
 

<h3>2.3 How to control cookies</h3>

Unless you have adjusted your browser settings so that it will refuse cookies, our system will issue cookies as soon as you visit our websites. By using our websites you agree we can place cookies on your device.
If you want to block cookies, remove existing cookies or change your cookies settings, you can do so via your browser settings. These settings can be found in the “options” or “preferences” menu of your internet browser.
 
<br><br>
For details, see <a href="http://www.aboutcookies.org/">aboutcookies.org</a>. 
<br><br>

You can delete all cookies that are already on your computer and you can set most browsers to prevent them from being placed.
If you do this, however, you may have to manually adjust some preferences every time you visit our sites and as a consequence, 
some services and functionalities may not work.
However, remember that, if you turn off cookies, certain parts of our sites, some graphic elements may not be accessible 
 or not look as good and you may not be able to use certain applications.
<br><br>


<h2>3. How We Use Information</h2>  

We use the information we collect in various ways, including to: 
<br><br>
- Provide, operate, and maintain our Services <br>
- Improve, personalize, and expand our Services <br>
- Understand and analyze how you use our Services <br>
- Develop new products, services, features, and functionality <br>
- Communicate with you directly regarding our services, or in response to a query you have raised<br>
- Announce that something on our sites has changed via social media sites.
<br><br>

<h2>4. How We Share Information</h2> 

If you choose to create an account, the information you provide to us, including text and images
 you contribute related to our database, will be available to view. 
We will never sell-on the user registration details (your name, your email address) which you provide us,
 when you sign up to our database.
<br><br>
Additionally, advertisers and analytics providers may share and use the information they gather automatically  
<br><br>

<h2>5. Security</h2>   

We are committed to protecting your information. To do so, we employ a variety of security technologies and 
measures designed to protect information from unauthorized access, use, or disclosure. 
The measures we use are designed to provide a level of security appropriate to the risk of processing your personal information.
However, please bear in mind that the Internet cannot be guaranteed to be 100% secure.
<br><br>

<h2>6. Your Data Protection Rights </h2>  

If you wish to amend or request deletion of your personal information, 
you can do so at any time by <a href=/database/main/contact.php>contacting us</a>.
Upon receipt, we will use reasonable effort to satisfy your request within 1 month. 
