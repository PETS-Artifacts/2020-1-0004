<p>
When making tour payments and deposits, we collect some personal information, depending on how the payment is made:<br>
<br>
Our two credit card processors, Moneris Systems and PayPal, capture credit card payment information via 1shoppingcart.com and mcssl.com, and provides SouthAmerica.travel with access to address information, as well as the first and last 4 digits only of the cardholder's credit card number. This information is stored on the merchant services' secure servers only, and is used for reference only. Credit card information is never added to the users file. Address information may be added to the user's file for distribution of tour vouchers and flight tickets.<br>
