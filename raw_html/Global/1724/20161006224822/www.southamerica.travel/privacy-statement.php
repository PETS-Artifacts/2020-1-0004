SouthAmerica.travel has created this privacy statement in order to demonstrate our firm commitment to privacy. The following discloses our information gathering and dissemination practices for http://www.SouthAmerica.travel, http://news.SouthAmerica.travel and all related websites.<br>
<br>
We shall never share your personally identifiable information (name, postal address, phone number, email address, etc.) with any third party. We shall never contact you without your explicit permission, except to inform you of changes or specials.<br>
<br>
We use your IP address to help diagnose problems with our server, and to administer our Web site. Your IP address is used only for statistical purposes.<br>
<br>
Our site's registration form encourages users to give us contact information (like their name and email address). We use customer contact information from the registration form to send the user information about our company and other travel-related news. The customer's contact information is also used to contact the visitor when necessary, for example, to deliver flight tickets. Users may opt-out of receiving future mailings.<br>
<br>
Our site uses an order form for customers to request information, products, and services. We collect visitor's contact information (like their email address) and demographic information (like their zip code). Contact information from the order form is used to send orders and information about our company to our customers. The customer's contact information is also used to get in touch with the visitor when necessary. Financial information that is collected is used to check the users' qualifications and bill the user for products and services. Demographic and profile data is also collected at our site. We use this data to tailor our visitor's experience at our site, showing them content that we think they might be interested in, and displaying the content according to their preferences.
