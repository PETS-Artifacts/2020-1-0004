               <h2>European Union General Data Protection Regulation (EU GDPR) Privacy Notice</h2>
               
               <p><strong>(Lawful Basis for Collecting and Processing of Personal Data)</strong></p>
               
               <p>Kennesaw State University is an institute of higher education involved in education,
                  research, and community engagement. In order for KSU to educate its students both
                  in class and on-line, engage in world-class research, and provide community services,
                  it is essential, necessary, and KSU has lawful bases to collect, process, use, and
                  maintain data of its students, employees, applicants, research subjects, and others
                  involved in its educational, research, and community programs. The lawful bases include,
                  without limitation, admission, registration, delivery of classroom, on-line, and study
                  abroad education, grades, communications, employment, applied research, development,
                  program analysis for improvements, and records retention. Examples of data that KSU
                  may need to collect in connection with the lawful bases are: name, email address,
                  IP address, physical address or other location identifier, photos, as well as some
                  sensitive personal data obtained with prior consent.
               </p>
               
               <p>For more information regarding the EU GDPR, please review Kennesaw Stateâ€™s <a href="http://gdpr.kennesaw.edu/">EU General Data Protection Regulation Compliance Policy.</a></p>
               
               <p>The majority of KSUâ€™s collection and processing of personal data will fall under the
                  following categories:
               </p>
               
               <ol>
                  
                  <li>Processing is necessary for the purposes of the legitimate interests pursued by KSU
                     or third parties in providing education, employment, research and development, and
                     community programs.
                  </li>
                  
                  <li>Processing is necessary for the performance of a contract to which the data subject
                     is party or in order to take steps at the request of the data subject prior to entering
                     into a contract. This lawful basis pertains primarily but not exclusively to research
                     contracts.
                  </li>
                  
                  <li>Processing is necessary for compliance with a legal obligation to which KSU is subject.</li>
                  
                  <li>The data subject has given consent to the processing of his or her personal data for
                     one or more specific purposes.&nbsp; This lawful basis pertains primarily, but not exclusively,
                     to the protection of research subjects.
                  </li>
                  
               </ol>
               
               <p>There will be some instances where the collection and processing of personal data
                  will be pursuant to other lawful bases.
               </p>
               
               <h2>Individual Rights of the Data Subject under the EU GDPR</h2>
               
               <p>Individual data subjects covered by KSUâ€™s EU General Data Protection Regulation Compliance
                  Policy will be afforded the following rights:
               </p>
               
               <ul>
                  
                  <li>information about the controller collecting the data</li>
                  
                  <li>the data protection officer contact information</li>
                  
                  <li>the purposes and legal basis/legitimate interests of the data collection/processing</li>
                  
                  <li>recipients of the personal data</li>
                  
                  <li>if KSU intends to transfer personal data to another country or international organization</li>
                  
                  <li>the period the personal data will be stored</li>
                  
                  <li>the existence of the right to access, rectify incorrect data or erase personal data,
                     restrict or object to processing, and the right to data portability
                  </li>
                  
                  <li>the existence of the right to withdraw consent at any time</li>
                  
                  <li>the right to lodge a complaint with a supervisory authority (established in the EU)</li>
                  
                  <li>why the personal data are required, and possible consequences of the failure to provide
                     the data
                  </li>
                  
                  <li>the existence of automated decision-making, including profiling</li>
                  
                  <li>if the collected data are going to be further processed for a purpose other than that
                     for which it was collected
                  </li>
                  
               </ul>
               
               <p><strong>Note: Exercising of these rights is a guarantee to be afforded a process and not the
                     guarantee of an outcome.</strong></p>
               
               <p>Any data subject who wishes to exercise any of the above-mentioned rights may do so
