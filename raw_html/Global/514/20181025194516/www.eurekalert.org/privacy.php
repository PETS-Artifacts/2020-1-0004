<h1 class="pageheadsolo">Privacy Policy</h1>

<p><b>Posted: October 4, 2018
<br />Effective: October 4, 2018</b></p>

<p>The American Association for the Advancement of Science ("AAAS") is firmly committed to protecting the privacy of its users. This privacy policy ("Policy") describes how AAAS collects, uses, and shares information, including personal information, when you visit the EurekAlert! website, www.eurekalert.org ("Site"). It also provides a means for you to "opt out" of providing certain personal information.</p>

<h4><br />Information We Collect</h4>

<p>The Site is divided into two areas:</p>

<ul>
<li>A public area accessible to anyone with world wide web resources, and</li>
<li>A restricted area open to registered journalists and public information officers (PIOs).</li>
</ul>

<p><br /><u>Information Collected Automatically</u></p>

<p>When you visit the Site, the following information is collected automatically as part of the Site's operation:</p>

<ul>
<li><b>Device information</b>: We collect information about your device, such as the type of browser and operating system you're using and your device's IP address.  </li>
<li><b>Usage information</b>: We collect information such as the name of the website that referred you to us (the last website you visited before this one) and the date and time you access the Site and the duration of your visit.</li>
</ul>

<p>This information is collected and used in the aggregate only and is not used to identify you personally. Such information enables us to personalize our content to users' needs and to help those organizations that post information to the Site to better understand the demographics of those seeing their materials.</p>

<p><br /><u>Information You Provide</u></p>

<p>Personal information is any information that relates to an identified person or information which, together with other information, may identify a person.  Below we describe the types of personal information that we collect, how that information is used, and the legal basis for the collection of such information.</p>

<ul>
<li><b>Registration information</b>: If you register for an account, we ask for your first and last name, telephone number, email address, and physical address. We also ask for information about your job in order to confirm that you are eligible to create an account on the Site. </li>
<li><b>Contact information</b>: If you choose to contact us or make a purchase on the Site, we may ask for your name, telephone number, email address, and physical address.</li>
<li><b>Payment information</b>: We ask for your payment card information if you choose to purchase products or services on the Site. </li>
</ul>

<p>On some occasions we may ask you to provide personal information on online forms. Providing information on online forms is always voluntary, and you are free not to complete any online forms. If you choose not to complete the forms, however, some products and services may not be available to you.</p>

<h4><br />How We Use Your Information</h4>

<p>We use the information that we collect to do the following:</p>

<ul>
<li>Administer your account;</li>
<li>Process your transactions or fulfill your requests;</li>
<li>Send you information about your relationship or transactions with us;</li>
<li>Process and respond to your inquiries;</li>
<li>Contact you with information that we believe will be of interest to you, such as special events and promotions, new products or services, or other information that may be of interest;</li>
<li>Generate and review reports and data about Site users;</li>
<li>Perform analytics and research aimed at improving the accuracy, effectiveness, usability, or popularity of the Site;</li>
<li>Improve the content and features of the Site or develop new content and features;</li>
<li>Enforce the legal terms that govern your use of the Site;</li>
<li>Administer and troubleshoot the Site; and</li>
<li>For the specific purpose for which you provide information which will be disclosed at the time you provide the information.</li>
</ul>

<h4><br />How We Share Your Information</h4>

<p>We may disclose your personal information as follows:</p>

<ul>
<li>With third-party service providers that provide certain services related to our Site; </li>
<li>With journal administrators if you create an account and request access to a journal so that you may be granted access to such journal;</li>
<li>With third parties in response to any subpoenas, court orders, or other legal process we receive; to establish or exercise our legal rights; to defend against legal claims; or as otherwise required by law;</li>
<li>With third parties such as law enforcement, government agencies, courts, and/or other organizations when we believe in good faith that a disclosure is appropriate in order to investigate, prevent, or take action against possible violations of this Policy or the Site Terms & Conditions and/or to protect the rights and property of AAAS, our employees, users, or the public;</li>
<li>With third parties such as law enforcement, government agencies, courts, and/or other organizations when we believe in good faith that a disclosure is appropriate in order to investigate, prevent, or take action regarding possible illegal activities, suspected fraud, or situations involving potential threats to the physical safety of any person; </li>
<li>With third parties in connection with any proposed or actual merger, reorganization, transfer of control, a sale of some or all of our assets, or a financing or acquisition of all or a portion of our organization by another company; and</li>
<li>With any third parties if we have your consent to make such a disclosure.</li>
</ul>

<p>We may also share certain information if we have reason to believe that an individual has violated our embargo policy by obtaining material via someone else's password-approved access. We would only do this in response to a specific request by the journal or research institution that submitted the embargoed document, or by the account holder whose password-approved access may have been compromised, in order to review the embargo violation incident. However, AAAS reserves the right to decline any of these requests if there is not sufficient evidence, in AAAS' sole judgment, to support the charge. If we agree to provide information related to an alleged embargo violation, we will disclose:</p>

<ul>
<li>Whether or not the person viewed the document for which the embargo was broken;</li>
<li>If so, what time of day he or she viewed it; </li>
<li>Whether his or her request for the file, if made, was successfully fulfilled; and</li>
<li>Other information specifically related to the incident that, in AAAS' sole judgment, is appropriate or necessary to be disclosed. </li>
</ul>

<p>If AAAS decides to fulfill such a request by a journal or an institution, the user in question will be notified.</p>

<h4><br />Our Legal Bases for Processing and Sharing Your Information</h4>

<p>We process your information and share it with third parties for the purposes described in this Policy, based on the following legal grounds:</p>

<ul>
<li><b>With your consent</b>: We ask for your consent to process or share your information for specific purposes and you have the right to withdraw your consent at any time. For example, we ask for your consent to provide you with promotional information. We also ask for your consent to collect information through surveys. </li>
<li><b>For our legitimate interests</b>: We process and share your information for our legitimate interests and those of third parties while applying appropriate safeguards that protect your privacy. For example, we process and share your information in order to help us:
  <ul>
    <li>Provide, maintain, and improve the Site; </li>
    <li>Perform analytics and research aimed at improving the accuracy, effectiveness, usability, or popularity of the Site; </li>
    <li>Improve the content and features of the Site or develop new content and features; </li>
    <li>Detect, prevent, or otherwise address fraud, abuse, security, or technical issues with the Site; </li>
    <li>Protect against harm to the rights, property or safety of AAAS, our users, or the public as required or permitted by law; and</li>
    <li>Enforce legal claims, including investigation of potential violations of applicable Site Terms & Conditions.</li>
  </ul>
</li>
<li><b>To fulfill our contractual obligations</b>: We process and share your data to provide a service you have requested under a contract. For example, we process your payment information when you make a purchase on the Site.</li>
<li><b>To comply with legal obligations</b>: We process and share your data when we have a legal obligation to do so, for example, if we're responding to legal process or an enforceable governmental request.</li>
</ul>

<h4><br />Transfer of Personal Data Outside the EEA</h4>

<p>Personal data captured on this website may be transferred to, and stored or otherwise processed at, a destination outside the European Economic Area (hereinafter "EEA"), including destinations that are not subject to Data Privacy Provisions equivalent to those in the EEA countries in which you provided your personal data.  We transfer your data to the US with your consent or where necessary to provide you with the services you have requested.  We will take all steps reasonably necessary to ensure that your personal data is processed safely and in accordance with this Privacy Policy.</p>

<h4><br />About Cookies</h4>

<p>When you visit the Site, we or our third-party providers place a text file called a "cookie" in the browser directory of your device's hard drive. These may be session cookies, which expire once you close your browser, or persistent cookies, which stay on your device for a set period of time or until you delete them. We use these cookies to support the functionality of the Site, including adjustments to content. Below is more detail about the types of cookies that we use:</p>

<ul>
<li><b>Required</b>: These cookies are required for the functioning of the Site and may be used to implement security features or embed video or sound files.</li>
<li><b>Functional</b>: These cookies record some of your activity on the website as well as information about your device, such as what browser you are running, and what pages you access. This information helps us and our third-party service providers diagnose server and software errors, and, in cases of abuse, track and mitigate the abuse.</li>
<li><b>Analytics</b>: Our service providers, including Google Analytics, use cookies as part of their tools. These cookies allow our service providers and us to recognize and count the number of users of the Site, see how they interact with the Site and different functions, and determine how to improve the Site.</li>
</ul>

<p>We use a tool called "Google Analytics" to collect some information we listed above about your use of the Site. We use the information we get from Google Analytics to improve the Site.  In order to collect this information, Google Analytics may set cookies on your browser or mobile device, or read cookies that are already there. Google Analytics may also receive information about you from apps you have downloaded that partner with Google. We do not combine the information collected through the use of Google Analytics with personal information. Google's ability to use and share information collected by Google Analytics about your visits to the Site with other websites which partner with Google is restricted by the <a target="_blank" href="https://www.google.com/analytics/terms/us.html">Google Analytics Terms of Service</a> and the <a target="_blank" href="http://www.google.com/policies/privacy/partners/">Google Privacy Policy</a>. Please review those for information about how Google uses the information collected through Google Analytics and how you can control the information collected by Google. </p>

<h4><br />Opting Out of Tracking and Email Communications</h4>

<ul>
<li><b>Opting out of cookies</b>: You may set your browser to decline cookies. However, if you elect to decline cookies, some pages of the Site may not display properly. You are free to delete cookies after your session, and your browser should contain instructions on how to do this.</li>
<li><b>Google Analytics</b>: To prevent your information from being used by Google Analytics, you can download the Google Analytics opt-out browser add-on which can be found <a target="_blank" href="https://tools.google.com/dlpage/gaoptout/">here</a>.</li>
<li><b>Email communications</b>: When you provide your email address when registering for an account, making a purchase, signing up for our mailing list, or otherwise, we may begin sending you marketing messages. If you do not want to receive marketing message from AAAS, simply follow the Unsubscribe instructions at the end of any email. You can also opt out at any time by emailing webmaster@eurekalert.org. Please note that if you opt out of marketing messages, we may still send you messages about your account or transactions. </li>
</ul>

<h4><br />Updating Your Account Information</h4>

<p>You may update the contact information and other personal information in your account at any time by going to the Site and clicking on the My Settings button. However, only an AAAS staff member may approve your changed status (i.e., reporter, freelancer, or PIO). Requests for change of status should be made to webmaster@eurekalert.org. We may also modify the way your information appears in your profile to conform to our standards of style.<//p>

<h4><br />How Long We Retain Your Information</h4>

<p>We retain indefinitely all the personal information we gather about visitors or subscribers as necessary to fulfill our contract with you or to process future requests for account activation. We retain indefinitely certain anonymized information we gather about visitors or subscribers for the legitimate purpose of performing website analytics and providing use of our website to all visitors in an efficient, practical, and relevant way. If there is no legal basis or other requirement for keeping your data AND after there has been no activity from you for twenty-six (26) months, your data that is no longer required will be purged. You may also remove yourself from AAAS's database at any time by contacting us via e-mail (webmaster@eurekalert.org); telephone (1-202-326-6716); fax (1-202-898-0391) or mail: </p>

<p>EurekAlert!
<br />American Association for the Advancement of Science
<br />1200 New York Ave., NW
<br />Washington, DC 20005</p>

<p>Please note that even if you request that we delete your information, deletion by our third party providers may not be immediate and the deleted information may persist in backup copies for a reasonable period of time. Also, information that has already been shared with third parties may not be deleted by them.</p>

<h4><br />Security</h4>

<p>The security of your personal information is important to us. We maintain reasonable physical, technical, and administrative security measures to protect and limit access to your personal information. However, no method of transmission over the Internet or electronic storage technology is 100% secure. While we strive to use commercially acceptable means to protect your personal information, we cannot guarantee its absolute security.</p>

<h4><br />Third-Party Data Collection and Links to Other Websites</h4>

<p>Some content or applications on the Site may be provided by third parties, including analytics companies and advertising providers.  These third parties may use cookies or other tracking technologies to collect information about you when you use the Site. The information they collect may be associated with your personal information or they may collect information, including personal information, about your online activities over time and across different websites and other online services. For information on how to opt out of such tracking, see the "Opting Out of Tracking and Email Communications" section above.</p>

<p>The websites to which we link may have different privacy policies and practices from those disclosed here. We assume no responsibility for the policies or practices of linked websites and encourage you to become acquainted with them.</p>

<h4><br />"Do-Not-Track" Disclosure</h4>

<p>The Site does not recognize or respond to browser-initiated Do Not Track signals.</p>

<h4><br />Children</h4>

<p>The Site is not intended for use by children. We do not knowingly collect personal information from children under the age of 13 years. Please contact us if you believe we may have unknowingly collected information about a child under 13, and we will take immediate steps to delete any such personal information.</p>

