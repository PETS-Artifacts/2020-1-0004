<p>If EU data protection laws apply to our processing of your personal information, you have the right to access, correct, amend, or delete personal information about you that we hold. Further information about your rights is below. You may access, correct, or delete information about you by contacting us at dataprotectionoffice@aaas.org. For assistance with exercising your rights please write us at:</p>

