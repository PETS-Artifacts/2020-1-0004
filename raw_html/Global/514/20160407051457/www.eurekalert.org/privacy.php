<h1 class="pageheadsolo">Privacy Policy</h1>

<p> EurekAlert!/AAAS is firmly committed to
protecting the privacy of its users.  </p>
<P>
<ol>
  <li> <strong>Information Gathered by EurekAlert!/AAAS</strong> 
       <p>EurekAlert! is divided into
       two areas: 
       <ol>
	 <LI> A public area accessible to anyone with world wide web
	      resources, and 
	 <LI> A restricted area open to registered journalists and
	      public information officers (PIOs). 
       </ol>

<p>EurekAlert!/AAAS gathers two types of information:
       
       <ol>
	 <li> Individual data that journalists and PIOs provide when
