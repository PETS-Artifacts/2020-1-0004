<p>We have reviewed our data collection and retention policy in line with the EU's General Data Protection Act (GDPR) and all users of <i>esl-lounge.com</i> will have the same data protection, regardless of location.</p>

<p>The GDPR deals with how Personal Information is handled. This is data that can be traced back to an individual - you! This may include everything from names and email addresses to biometric data and criminal record information. This Privacy Policy lays out how that data is handled: how it is collected, for what reasons and what rights you have over that data.</p>


<h2>The Information We Collect</h2>


<p><b>Personal Data</b></p>

<p>We will collect your name and your email address if you do one of the following:</p>

<ul style="list-style-type:none;">
  <li>a) Sign up the <a href="/premium/">Premium Site</a>.</li>
  <li>b) Sign up to either the <a href="newsletter.php">Teacher</a> or <a href="/student/newsletter.php">Student Newsletter</a>. (using a double opt-in confirmation)</li>
  <li>c) Contact us and choose to submit your name and email address.</li>
</ul>

<p>When you sign up to the Premium Site and when you contact us, we will ask for your consent to use your personal data in our dealings with you.</p>


<p><b>Cookies</b></p>

<p>Cookies are very small text files that are stored on your computer when you visit some websites. </p>

<p>We use the following types of cookies on <i>esl-lounge.com</i>:</p>

<p>1. Session Cookies. These are used to control the membership section of the Premium Site. These are strictly necessary for the membership site to work properly, so if you disable them then certain functionalities will break or be unavailable.</p>

<p>2. Functional. These cookies allow for the efficient running of the website and may store sitewide preferences, cap the frequency of advertising or allow you to alter language and font settings. These cookies do not track personally identifiable information. They collect information in an anonymous form.</p>

<p>3. Analytics. We use Google Analytics to carry out 'benign' tracking that enables us to better manage the site to cater for our visitors. This tells us, for example, which pages are loading slowly, which countries are supplying more or fewer visitors, which mobile devices are being used most to access the site, and so on. You can read <a href="https://www.google.com/analytics/terms/us.html" target="_blank">Google Analytics Terms and Conditions</a> to understand this better.<!-- We anonymise user IP addresses, so no personally indentifiable information is passed to our analytics service. --></p>

<p>4. Social. We use Facebook and Twitter buttons on the site to enable our users to share pages with others across those platforms. If you want to block third-party cookies such as these, you can do so easily from <a href="https://www.google.com/search?q=block+cookies+in+browser" target="_blank">within your browser</a>.</p>

<p><b>Personalised Advertising</b></p>

<p>We do not use cookies to show personalised ads to those users who are in jurisdictions where explicit consent would be required.</p>


<p><b>Log Files</b></p>

<p>There is information about your computer hardware and software that is automatically collected. This information can include: your IP address, browser type, pages visited, files downloaded, screen size, access times and referring Web site addresses. There is no way for us to resolve IP addresses to identify individual persons, something that would only be possible were we an ISP company.</P>

<p>This information is used by <i>esl-lounge.com</i> for the operation of the service, to maintain and improve quality of the service, and to provide general statistics regarding use of the <i>esl-lounge.com</i> site.</p>



<h2>Our Use Of Your Information</h2>

<p>We do not repurpose the personal data that we have collected.</p>

<p>For example, we have never, since we opened in 2001, used email addresses from contact form submissions for marketing.</p>

<p>If you contact us via the contact forms, we will use your name and email address solely for communicating with you and dealing with any questions you had. When you write to us, you will be asked for consent for us to use your data in this way.</p>

<p>Personal information used for Premium Site sign ups are only used for membership registration and are also not used for marketing purposes. Notifications of new lessons on the Premium site can be sent to these email addresses. Permission to use your email addresses is asked explicitly at registration, in compliance with the GDPR.</p>



<h2>Our Legal Basis For Using Your Information</h2>

<p>This section explains the reasons we use your personal information.</p>

<p>1. Consent: Where required by law, we handle personal data on the basis of your implied or express consent. You will, for example, be asked for consent when using the forms on our 'contact us' pages or when you sign up to the Premium site.</p>

<p>2. To honor our contractual commitments to you: provision of the Premium Site service, for example, necessitates the collection and use of certain personal data.</p>

<p>3. Legitimate interests: In some cases, we handle personal data on the ground that it furthers our legitimate interests. This includes: Providing a safe and enjoyable user experience; Customer service; Analyzing and improving our business.</p>

<p>4. Legal compliance: We may need to use and disclose personal data in certain ways to comply with our legal obligations, for example to protect the safety of somebody.</p>



<h2>Our Disclosure Of Your Information</h2>

<p>When you sign up to the Premium site, payment data (which could be name, address, payment card information) is not seen by anyone on <i>esl-lounge.com</i> but is instead processed on a third party payment processing site. This allows us to process registration and provide the paid membership service.</p>

<p>All PayPal&reg; transactions, for example, are subject to the <a href="https://www.paypal.com/uk/webapps/mpp/ua/privacy-prev" target="_blank">PayPal Privacy Policy</a>. We also use 2Checkout, who are <a href="https://www.2checkout.com/policies/privacy-policy" target="_blank">fully GDPR compliant</a>, for credit card payments.</p>



<h2>Children</h2>

<p><i>esl-lounge.com</i> is a website and service intended for an older audience and we do not provide the service for those under the age of 18. We do not knowingly collect any personal information from those under the age of 18. If you have become aware that we may have collected the personal data of someone under the age of 18, please contact us and we will endeavor to verify this and to delete all such data.</p>



<h2>Data Retention</h2>

<p>Log files are stored on our host's servers, but are deleted after 30 days.</p>

<p>The personal data of incomplete Premium Site registrations is deleted after three months.</p>



<h2>Security</h2>

<p>Those using our services should take all precautions not to include sensitive data (home addresses, credit card details, etc) in their correspondence with <i>esl-lounge.com</i>. Email is not to be considered a highly secure form of communication.</p>



<h2>Your Rights Over Your Data</h2>

<p><b>How Can I Access The Personal Data You Have About Me?</b></p>

<p>If you reside in the EEA, you may have the right to see the data that we hold about you. You can <a href="contact.php">contact us</a> and we will send the data, in a portable form, in fewer than 30 days. As outlined above, this personal data, on our site, is highly unlikely to be anything more than your name and email address if you have previously contacted us.</p>

<p><b>Can I Alter And Amend The Data?</b></p>

<p>After you have seen the data we hold on you, you may have, depending on where you reside, the right to confirm its validity and ask for corrections to be made. We will firstly carry out some checks to verify your identity to prevent malicious actions.</p>

<p><b>Can I Have The Data Deleted?</b></p>

<p>After a standard verification procedure, you will have the right to request to have this personal information regarding you deleted.</p>



<h2>Your Right To Opt-Out</h2>

<p>We require a double opt-in before we send anyone the teacher or student newsletter. This means that even after you fill out the subscribe form on the site, we will send you another email, just to check, just to say "are you sure?". That is good standard practice in the world of newsletters and mailing lists and we have done this since 2001. You have the right to opt out of these (very!) occasional mailings using a link found at the bottom of every newsletter.</p>

<p>If you are member of the Premium site, you also have the right to opt out of our newsletter notifications of new lesson plans having been placed on the site. You also have the right to request the deletion of your account at any time, although as a paid membership site, that will not give you the right to a refund.</p>



<h2>Can I Block Cookies?</h2>

<p>If you want to block all cookies from our site, this can be done very simply from <a href="https://www.google.com/search?q=block+cookies+in+browser" target="_blank">within your browser settings</a>. This may affect your use of the site, especially if you are a member of the Premium Site. Blocking session cookies on the Premium Site, for example, will cause problems for those wishing to log in.</p>



<h2>External Links</h2>

<p>This site contains links to other sites. <I>esl-lounge.com</I> is not responsible for the privacy practices or the content of such Web sites. </P>



<h2>Contacting Us</h2>

<p>If you have any questions about our Privacy Policy, if you would like clarification on anything here detailing how we use personal information, or if you would like to know something about the data we hold on you, don't hesitate to <a href="contact.php">contact us</a>.</p>


