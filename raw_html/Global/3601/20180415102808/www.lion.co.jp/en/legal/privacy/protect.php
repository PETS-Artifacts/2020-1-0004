<h3 class="mod-heading4">3. Disclosure and Provision of Personal Information to Third Parties</h3>
<p class="mod-p">
Lion shall not disclose or provide personal information to third parties (including third parties outside Japan), except under the following conditions. </p>
<ol class="mod-ol with-brackets"><li>The persons concerned have given permission</li>
<li>Lion outsources operations related to the handling of personal information in whole or in part (in such cases, Lion shall properly supervise the contractor, including such measures as forming contracts related to the protection of personal information)</li>
<li>Information is disclosed or provided in a form in which the individual identities of persons concerned cannot be discerned, such as statistical data</li>
<li>Lion is compelled by law to disclose or provide personal information</li>
<li>It is necessary to protect human life, physical safety or property, and the persons concerned cannot be reached to obtain their prior permission</li>
<li>Lion is compelled to cooperate with governmental or other public entities in the execution of their duties and obtaining permission from the persons involved could impede said execution.</li>
</ol>


<h3 class="mod-heading4">4. Preparation and Provision of Anonymously Processed Information</h3>

<ol class="mod-ol with-brackets"><li>When preparing anonymously processed information, Lion shall do the following<br>(1)	Process the information appropriately to ensure that the individuals cannot be identified, etc.<br>
  (2) Implement safety control  measures to prevent information leaks<br>
  (3) Publicly disclose the items of information included in the anonymously processed information<br>
  (4) Refrain from comparing anonymously processed information with other information to identify individuals</li>
<li>When providing anonymously processed information to third parties, Lion shall publicly disclose the items of personal information contained therein and the methods by which the information will be provided, and shall clearly communicate to the third parties receiving the information that said information is anonymously processed.</li>
</ol>


<h3 class="mod-heading4">5. Personal Information Management</h3>
<p class="mod-p">Lion shall manage personal information it has received from persons concerned in accordance with relevant laws and standards as well as its internal rules. Furthermore, Lion shall take proper and reasonable safety measures to prevent the illegal access, loss, destruction, alteration or leakage of personal information. In the event that such an event should occur, Lion shall quickly take corrective measures.</p>




<h3 class="mod-heading4">6. Requests for the Disclosure, Etc., of Personal Data</h3>
<p class="mod-p">If the persons concerned or their representatives request the disclosure, correction, deletion or suspension of use, etc. (hereinafter &ldquo;disclosure, etc.&rdquo;) of personal data Lion has received from said persons, after verifying their identity, Lion shall respond to such requests within a reasonable time period and scope. <br>
However, Lion will not disclose such data in the circumstances described below. When Lion decides to not disclose such data, it shall notify the requester of its decision and the reason for the decision.
</p>

<ul class="mod-ul">
<li>Lion cannot confirm that the requester is the person that the retained personal data concerns</li>
<li>Lion cannot confirm the right of representation claimed by a representative seeking to submit the request on behalf of the persons concerned</li>
<li>The request form is incompletely or incorrectly filled out, or the request has not been made in accordance with the methods specified by Lion</li>
<li>The information subject to the request for disclosure, etc., is not classified as retained personal data</li>
<li>Disclosure could cause harm to the life, safety or property, or otherwise infringe the rights of the persons concerned or a third party</li>
<li>Disclosure could cause significant impediment to the proper execution of Lion's business</li>
<li>Disclosure would violate other laws or regulations</li>
<li>Any other circumstances in which Lion is not obligated to provide the disclosure, etc., of the data under the Act on the Protection of Personal Information</li>
</ul>


