<h3 class="mod-heading4">9. Suspension of Use and Elimination</h3>
<p class="mod-p">
If persons concerned request the suspension of use or elimination (hereinafter, “cessation of use”) of part or all of the retained personal data pertaining to themselves listed above in 6., after verifying their identity, Lion shall cease to use it within a reasonable time period and scope. <br>
Please understand that if Lion ceases to use part or all of said data, Lion may no longer be able to provide requested services to the persons concerned. <br>
Furthermore, Lion will not cease to use such data under the following circumstances. When Lion decides to not cease use of such data, it shall notify the requester of its decision and the reason for the decision.</p>

<ul class="mod-ul">

<li>Lion cannot confirm that the requester is the person that the retained personal data concerns</li>
<li>Lion cannot confirm the right of representation claimed by a representative seeking to submit the request on behalf of the persons concerned</li>
<li>The request form is incompletely or incorrectly filled out, or the request has not been made in accordance with the methods described in 10., below</li>
<li>The information subject to the request for cessation of use does not fall under retained personal data</li>
<li>The reason for the request for cessation of use is not one of the following
<ol class="mod-ol">
<li>Information is being used for purposes other than those for which it was obtained</li>
<li>Information was obtained improperly</li>
<li>Restrictions on provision to third parties have been violated</li>
</ol>
</li>
<li>The facts related to the reason given for the request for cessation of use are not found to be correct</li>
<li>Lion is obligated by law to retain the information </li>
<li>Any other circumstances in which Lion is not obligated to cease use of the data under the Personal Information Protection Law</li>
</ul>


