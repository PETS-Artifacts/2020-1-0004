<h3 class="mod-heading4">2. Personal Information Management</h3>
<p class="mod-p">
Lion shall manage personal information it has received from persons concerned in accordance with related laws and standards as well as its internal rules.<br>
Lion shall take proper and reasonable safety measures to prevent the illegal access, loss, destruction, alteration or leakage of personal information. In the event that such an event should occur, Lion shall quickly take corrective measures.</p>


<h3 class="mod-heading4">3. Personal Information Use</h3>
<p class="mod-p">
Lion shall not use personal information it has received from persons concerned for purposes other than those it has provided prior notification of in accordance with 1., above.<br>
Should Lion use personal information for purposes other than those for which it has given prior notification, or should the nature of the stated purposes itself change, Lion must give prior notice to the persons concerned.<br>
Furthermore, information that has become unnecessary in light of Lion's purposes of use shall be quickly and appropriately deleted or destroyed. </p>


<h3 class="mod-heading4">4. Disclosure and Provision of Personal Information to Third Parties</h3>
<p class="mod-p">Lion shall not disclose or provide personal information to third parties, except under the following conditions.</p>

<ol class="mod-ol with-brackets"><li>The persons concerned have given permission</li>
<li>Lion outsources operations related to the handling of personal information in whole or in part (in such cases, Lion shall properly supervise the contractor, including such measures as forming contracts related to the protection of personal information)</li>
<li>Information is disclosed or provided in a form in which the individual identities of persons concerned cannot be discerned, such as statistical data</li>
<li>Lion is compelled by law to disclose or provide personal information</li>
<li>It is necessary to protect human life, physical safety or property, and the persons concerned cannot be reached to obtain their prior permission</li>
<li>Lion is compelled to cooperate with governmental or other public entities in the execution of their duties and obtaining permission from the persons involved could impede said execution.</li>
</ol>


