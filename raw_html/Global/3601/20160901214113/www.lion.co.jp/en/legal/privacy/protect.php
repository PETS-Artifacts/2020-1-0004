<section class="mod-section" id="sec1">
<h2 class="mod-heading3">Privacy Policy</h2>
<p class="mod-p">In handling the personal information of its customers in business operations, the Lion Group makes every effort to protect personal information through appropriate management based on internal rules. We highly value the dignity of individuals and the utility of information while strictly observing laws and social norms. </p>

<h3 class="mod-heading4">1. Strict Observation of Laws and Regulations and Continuous Improvement of the Management Structure</h3>
<p class="mod-p">
The Lion Group strictly observes laws and regulations related to personal information, and updates its internal systems and rules to protect personal information. While effectively administering personal information, we strive to continuously improve our information management structure.
</p>


<h3 class="mod-heading4">2. Acquisition and Use of Personal Information </h3>
<p class="mod-p">
When acquiring personal information, the Lion Group clearly states the purpose of obtaining the information and the scope of its usage in business activities. With the prior permission of customers, we appropriately and fairly acquire and use personal information within the limitations of the purpose for obtaining the information. 
</p>
<p class="mod-p">We do not share the personal information of our customers with third parties unless we have received permission from the customer in advance, or there are special legal or other justifiable reasons for doing so. </p>


<h3 class="mod-heading4">3. Ensuring the Accuracy and Security of Personal Information </h3>
<p class="mod-p">
To ensure the accuracy and security of personal information, the Lion Group makes every effort to maintain accuracy and update personal information. At the same time, the Lion Group strives to protect personal information by implementing appropriate and reasonable security measures to counter the risk of illegal access, loss, destruction, alteration, and leakage of information. </p>


<h3 class="mod-heading4">4. Responding to Customer Requests </h3>
<p class="mod-p">
When receiving a request from a customer about their personal information, the Lion Group promptly confirms the identity of the individual and responds appropriately to their request. 
</p>

