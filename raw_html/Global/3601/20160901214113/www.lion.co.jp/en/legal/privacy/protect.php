<h3 class="mod-heading4"> 7.	Disclosure</h3>
<p class="mod-p">
If persons concerned request the disclosure of retained personal data pertaining to themselves as listed above in 6., after verifying their identity, Lion shall respond to such requests within a reasonable time period and scope. <br>
However, Lion will not disclose such data in the circumstances described below. When Lion decides to not disclose such data, it shall notify the requester of its decision and the reason for the decision.</p>

<ul class="mod-ul">
<li>Lion cannot confirm that the requester is the person that the retained personal data concerns</li>
<li>Lion cannot confirm the right of representation claimed by a representative seeking to submit the request on behalf of the persons concerned</li>
<li>The request form is incompletely or incorrectly filled out, or the request has not been made in accordance with the methods described in 10., below</li>
<li>The information subject to the request for disclosure is not classified as retained personal data</li>
<li>Disclosure could cause harm to the life, safety or property, or otherwise infringe the rights of the persons concerned or a third party</li>
<li>Disclosure could cause significant impediment to the proper execution of Lion's business</li>
<li>Disclosure would violate other laws or regulations</li>
<li>Any other circumstances in which Lion is not obligated to disclose the data under the Act on the Protection of Personal Information </li>
</ul>
