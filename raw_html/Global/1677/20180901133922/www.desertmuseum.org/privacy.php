<h2>
Privacy Policy
</h2>
<h3>
Membership Acquisition:
</h3>
<p>
The Arizona-Sonora Desert Museum occasionally exchanges member name and address information, for one-time-use-only, with designated non-profit organizations to add members. If you prefer not to participate in this exchange, please opt-out by contacting the Museum's Member &amp; Donor Services Department at <a href="mailto:membership.privacy@desertmuseum.org">membership.privacy@desertmuseum.org</a>
</p>
<p>
The Desert Museum reserves the right to make changes to this policy at its sole discretion. Such revisions may arise in response to changes in the law, policy, or other factors. We encourage you to periodically visit this page to review our most current policy.
</p>
<h3>
Online:
</h3>
<p>
The Arizona-Sonora Desert Museum only has access to or collects information that you voluntarily give us via email or other direct contact from you.
</p>
<p>
We will not share your information with outside organizations.
</p>
<p>
We will use your information to respond to you, regarding the reason you contacted us.
</p>
<p>
You may opt out of receiving any future contacts from us at any time. This includes an "opt out notice" in any future email we may send you.
You can see what data we have about you, if any, and change/correct that data at any time by contacting us via phone at (520) 883-2702 or email at <a href="mailto:info@desertmuseum.org">info@desertmuseum.org</a>
</p>
<p>
Upon your request, we will delete any data we have about you at any time, if you contact us via phone at (520) 883-2702 or email at <a href="mailto:info@desertmuseum.org">info@desertmuseum.org</a>
</p>
