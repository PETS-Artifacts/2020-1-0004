        <h1>Privacy Statement and Security Policy</h1>
<h4>Privacy statement</h4>
        <p>This website (<a href="http://www.eia.gov/">http://www.eia.gov/</a>) is provided as a   public energy information service of the U.S. Energy Information Administration. 
        </p>
       <p class="right"><img src="/global/images/photos/privacy.jpg" alt="stock photo" width="250" height="166"/></p>
       <p>EIA.gov utilizes web measurement software tools in order to improve the efficiency of our website and to provide a better user experience for our customers.  If you visit this site to read or download information, you should know that EIA collects and stores a standard set of Internet-related information, such as an Internet Protocol (IP) address, the date and time, the type of browser and operating system used, the page(s) visited, and if you linked to the EIA website from another website, the address of that website.  </p>
       <p>The Office of Management and Budget (OMB) classifies this as a Tier 2 usage since we utilize multi-session web measurement technologies that do not collect any personally identifiable information (PII).   These technologies anonymously track how visitors interact with EIA.gov, including where they came from, what they did on the site, and whether they completed any pre-determined tasks while on the site. </p>
       <p>We use Google Analytics and IBM's NetInsight web measurement technologies.  This information is used to optimize our website; improve our user interface and ensure our content meets the needs of our customers. No personally identifiable information is collected, so the anonymity of the  user is protected.  The measurement data that is collected is only retained for as long as is needed for proper analysis and optimization of the website and is accessible only to employees whose position necessitates it. </p>
       <p>Since disabling this web measurement technology requires you to modify your individual browser settings, it is enabled by default.  If you wish to opt-out from these web measurement technologies, you can find instructions for changing your browser settings at <a href="http://www.usa.gov/optout_instructions.shtml">http://www.usa.gov/optout_instructions.shtml</a>.  Google also provides a browser plug-in that will allow you to opt-out of all Google Analytics measurements, which you can find at <a href="http://tools.google.com/dlpage/gaoptout">http://tools.google.com/dlpage/gaoptout</a>.  Please note that opting-out in no way effects your access to content or how you see the site. </p>
       <h4 style="display:inline;">If you send us personal information and/or subscribe to EIA's e-mail   notification system (e-lists)</h4>
        <p>If you choose to provide us with personal information such as submitting information through the website on our Feedback page (<a href="http://www.eia.gov/about/feedback/">www.eia.gov/about/feedback</a>), e-mail subscription lists, or in an e-mail to EIA's Information Center, or to any EIA email (@eia.gov) we use that information to respond to your message and to help us get you the information you have requested. We do not collect personal information for any purpose other than to respond to you. EIA does not give, sell, or transfer personal information to third parties, unless required by law, such as the Freedom of Information Act. </p>
        <h4>If you participate in  EIA's social media activities</h4>
        <p>
          EIA is committed to  expanding access to its information and engaging its customers and  stakeholders. One of the   key ways we seek to accomplish this is through  the use of third-party   social media websites and applications. We do not  collect or request   personally identifiable information through these outlets,  but may   occasionally come into contact with unsolicited PII due to    circumstances beyond our control. As a result, we reserve the right to   moderate  or remove comments that offer personally identifiable   information such as  address, phone number or social security number in a   public manner. No PII will  be retained in our system or shared with   outside parties.</p>
        <h4>Security, intrusion, and detection</h4>
        <p>For site security purposes, and   to ensure this   service remains available to all users, this Government computer     system employs software programs to monitor network traffic to identify     unauthorized attempts to upload or change information, or otherwise   cause   damage.</p>
        <h4>Operations security program</h4>
        <p>The U.S. Energy Information Administration     maintains a program (Operations Security [OPSEC]) that is designed to   protect   information collected in EIA's surveys. This Program takes the   form of various   elements such as: staff security training, pre-hire   certification, and integrity   of EIA data systems, fair practice data   disclosure policies, and the physical   security of all sensitive   unclassified EIA data. If you have any questions   concerning this   program, please contact Keith Rapoza, phone (202) 586-2626.</p>
        <h4>Links to other sites</h4>
        <p>Our website has links to many other websites including government (Federal, State, local, and international) agencies, universities, trade associations, energy companies, and selected other websites. All links that navigate you away from <em>www.eia.gov</em> are denoted by the <img src="/global/images/icons/external_link.png" alt="external link icon" width="8" height="8" style="float:none;padding:2px;margin:0;"/> icon.  If you navigate to another site, you will be subject to the privacy policy of that site.</p>

