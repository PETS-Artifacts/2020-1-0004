        <h3><strong><u>Group  Company Structure</u></strong></h3>
        <p>Society of Petroleum Engineers (SPE), Inc. is  the Data Controller for the purposes of the European Union General Data  Protection Regulation (&ldquo;GDPR&rdquo;). Society of Petroleum Engineers Europe Limited  is a wholly-owned subsidiary of SPEI and  is the place of central administration for SPE in the European Union (&ldquo;EU&rdquo;) and  as such is the &ldquo;Main Establishment&rdquo; for SPE in the EU.  SPEI is  located at 222 Palisades Creek Dr., Richardson, TX 75080-2040 USA.        </p>
        <p>SPE is a non-profit organization established to  collect, disseminate, and exchange technical knowledge and to provide information-sharing  opportunities concerning the exploration, development, and production of oil  and gas resources for the public benefit. Integral to our mission is the  facilitation of opportunities for energy professionals to enhance their  technical and professional competence through education and networking events.  We further offer opportunities for networking and education through our  volunteer program which helps members enhance engineering skills and increase  knowledge while sharing their expertise with others.</p>
        <p>Accordingly, the Personal Information that we  collect in furtherance of these objectives enables us to provide timely and  relevant information and content based on individuals&rsquo; technical interests and  geographic locations. This Policy will provide information regarding your  choices and rights regarding how we use this Personal Information. </p>
        <p>The scope of this Policy includes Personal Information  collected through SPE websites and subdomains, SPE mobile applications, events,  conferences, workshops, symposia, customer service call centers, email  correspondence, and any offline mechanisms including paper-based correspondence  and forms. Additionally, the scope of this Policy includes Personal Information  submitted to SPE in pursuance of employment opportunities. Separate privacy  policies exist for SPE employees, where appropriate. Some SPE affiliates may  have their own privacy policy. </p>
        <h3>&nbsp;</h3>
        <h3><strong>Collection  of Personal Information</strong></h3>
        <h4><strong>Personal Information that  we collect directly from you</strong></h4>
        <p><u>General</u> - We collect Personal Information such as your  address, email, telephone number(s), and other  contact demographic and billing information. </p>
        <p><u>Pursuing Employment at SPE</u> - We may receive Personal  Information from you including, but not limited to, resumes, CVs, cover letters  and other correspondence when you provide this information in pursuance of employment  at SPE. </p>
