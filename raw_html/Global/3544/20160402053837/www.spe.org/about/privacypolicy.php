        <p>From time to time, SPE will permit companies, who, in SPE's estimation, have something of value to offer to SPE members to rent mailing lists. However, SPE does not make available the email addresses of SPE members or customers for non-SPE business. Members and customers may choose to restrict use or disclosure of their name, address, or telephone number on mailing lists that may be rented to others.</p>
