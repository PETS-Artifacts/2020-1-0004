		<h1 class="page_summary">This Privacy Policy details what information SPE gathers about you and how we use it. The terms of our Privacy Policy apply to SPE members and customers, and whether SPE collects the information in electronic form through our website, our online services, or other internet processes or by paper.		</h1>
		<h3>Types of information we may gather</h3>
		<p>SPE collects information such as your address, telephone number(s), and other contact, demographic, and billing information only by voluntary disclosure directly from you. The SPE database contains information on members and nonmembers, including authors, exhibitors, advertisers, service providers, for the purpose of conducting official SPE business.</p>
		<h3>Examples of ways we collect your information</h3>
		<p>The <strong>Membership Application Form</strong> requires individuals to provide contact information such as address(es), phone number(s), and email address(es). This together with demographic information (i.e., job title, education, and business focus), SPE uses the information to provide member services, such as the <em>Journal of Petroleum Technology</em>, as well as catalogs and promotions of publications, meetings, and other products and information of interest to you.</p>
		<p>Information can be collected on an <strong>order form</strong>, an <strong>event registration form</strong>, or in another manner when <strong>information is requested</strong> or <strong>products and services purchased</strong> from SPE. SPE collects contact information (address, phone number, etc.), payment information (an account or credit card number) and other information. This information is used to fulfill orders and to contact the customer as necessary.</p>
        <p>SPE.org automatically records the <strong>Internet Protocol (IP)</strong> address of the computer accessing the site as well as the time and date of access. This information does not identify you personally, nor does it contain your name or email address. SPE uses this information solely for the purpose of diagnosing problems with the SPE server and to administer the website, or for some corporate subscribers, to determine access rights.</p>
        <p>SPE uses <strong>internet cookies</strong> to deliver content that is specific to your interests and to keep track of your preferences. When your browser accesses the site, SPE's server checks your browser settings to see if it is permissible to store this identification information on your computer. The cookies set by SPE do not personally identify you; they simply permit SPE's web site to know what your preferences are. Cookies are also used to help determine how many unique users access pages on the web site and may direct specific advertising content to you on selected pages.</p>
        <h3>How we use your information</h3>
        <p>The information SPE collects is intended to allow SPE to communicate with you, process your transaction effectively and efficiently and plan future programs and services to better serve you. SPE collects electronic information solely for the purpose of diagnosing problems with the SPE server and to administer the website, or for some corporate subscribers, to determine access rights.</p>
        <p>Members and customers may elect to have SPE not use their email address for SPE communications; however, an email address is required for the completion of certain transactions. Even when SPE members request that SPE restrict use of their personal information, SPE may still use this information to contact you in connection with SPE official business.</p>
        <p>If you choose to restrict use of your information, you may not receive certain communications from SPE that may be of interest to you. Because of the nature of computer data storage, any information that you restrict will remain stored but will not be accessed in the normal course of business. SPE will comply with any court orders that may be received requiring us to disclose information from our database, regardless of whether you have requested that the information be restricted.</p>
