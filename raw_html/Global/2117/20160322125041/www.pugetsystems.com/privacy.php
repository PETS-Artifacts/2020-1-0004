<h2>Our Commitment To Privacy</h2>
<p>Your privacy is important to us. To better protect your privacy we provide this notice explaining our online information practices and the choices you can make about the way your information is collected and used. </p>
<h2>The Information We Collect</h2>
<p>This notice applies to all information collected or submitted on the <a href="https://www.pugetsystems.com/">Puget Custom Computer System</a> website. We maintain a mailing list for news updates and hardware article releases. The only information collected for that purpose is your email address. For computer quote requests and orders, the types of personal information collected are:</p>
<ul>
<li>Name</li>
<li>Address</li>
<li>Email address</li>
<li>Phone number</li>
<li>Credit/Debit Card Information (orders only)</li>
<li>Account login and password</li>
</ul>
<h2>Data Security</h2>
<p>One of the biggest concerns in conducting business online is knowing that your credit card information is kept secure. Any time any personal information is submitted to us, our site uses full 128bit SSL encryption. This information is stored as plain text in our password protected SQL database, with the exception of credit card information. This information is encrypted before it is sent. Credit card information is not stored in our database, as we have no need to keep it once your order has been completed.</p>
<h2>The Way We Use Information</h2>
<p>We use the information you provide about yourself when placing an order only to complete that order. <B>We do not share this information with outside parties except to the extent necessary to complete that order.</B> We use return email addresses to answer the email we receive. Such addresses are not used for any other purpose and are not shared with outside parties.</p>
<p>Occasionally, we do send out a check-up email to all open quote requests. This email serves to give us information about which quotes continue to be active, and which quotes are closed and are no longer to be given our attention. Though these emails are sent from a script on our server, we handle every reply personally and requests for no further contact from us are promptly and personally fulfilled.</p>
<h2>Communication and Traffic Logs</h2>
<p>The logging system of Puget Custom Computer Systems is very extensive and is an essential asset to our analysis of our clients needs. It allows us to very accurately gauge the effectiveness of advertising campaigns, optimize our information presentation from traffic pattern analysis, and perform fraud investigations. Due to the complexity and detail of our logging, we feel it is necessary we explain what it is we do.</p>
<p>Our traffic logging is the most complex of our systems. In short, every page visit and click is logged. This is an enormous amount of data to log, but it allows us to make very detailed studies of client behavior and trends. The high resolution of this logging gives us a very good amount of information to aid our fraud investigations. All traffic on pugetsystems.com is also viewed in realtime by the operator of our "Live Chat" service.</p>
<p>When you first find our site, a variable called a "referrer" can tell us how you found us. We flag and log all external referrers to aid us in gauging the effectiveness of marketing campaigns.</p>
<p>Every email sent to and from pugetsystems.com is analyzed. If it can be affiliated with a client account, it is logged to that account and stored on our server. Email headers and email content is logged and available for our review at any time.</p>
