&#8212;How that collected information is used; and<br />
&#8212;How and to what extent our readers can opt-out of such data collection.</p>
<p><strong>What information do we collect?</strong></p>
<p>The only information we collect directly from any of our readers and users is the information that our web host and webserver automatically receives and records in data logs from your browser, such as your IP address and the page requested. This information is securely stored on our web host&#8217;s network and is necessary for the operation, maintenance, and security of our webserver and this website.</p>
<p>We do not use this collected information for any purpose other than serving our website pages to you. We do not use this information, or any information collected by our service providers or advertisers (as discussed below), for any purpose other than to better serve our readers. Pajiba does not rent, sell or otherwise share personal information about you with other people or companies other than the ones identified below unless we are responding to subpoenas, court orders, or legal processes, or to establish or exercise our legal rights or defend against legal claims.</p>
<p><strong>What third-party service providers do we use and what information do they collect?</strong></p>
<p>Our commenting platform is operated and maintained by Disqus, Inc. (&#8220;<strong>Disqus</strong>&#8221;). When you submit a comment or otherwise interact with the comments through Disqus&#8217; platform, Disqus may collect certain personal information of yours, including your name, e-mail address, and IP address. Please see <a href=" https://help.disqus.com/terms-and-policies/disqus-privacy-policy">the Disqus Privacy Policy</a> for more information including how to opt-out of Disqus&#8217; information collection. <br />
<br />
We also use certain analytics service providers to, among other things, understand what content our readers enjoy by tracking user-specific information on what pages users access or visit, maintaining records of past activity in order to provide better service when visitors return to our site, and using browser or device information to customize page content based on visitors&#8217; browser type or other information that the visitor sends. </p>
<p>These are the analytics service providers we currently use, either actively or from time-to-time, along with links to their respective privacy policies for more information including (where they provide such information) how to individually opt out of their analytics tracking:</p>
<p>&#8212;<strong>Google LLC</strong> - <a href="https://policies.google.com/technologies/partner-sites">Google Analytics&#8217; Privacy Policy, a.k.a., &#8220;how Google uses data&#8221;</a><br />
