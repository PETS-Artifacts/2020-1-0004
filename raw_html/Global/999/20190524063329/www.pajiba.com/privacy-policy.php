<p>From time to time, we may use customer information for new, unanticipated uses not previously disclosed in our privacy notice. If our information practices change at some time in the future we will post the policy changes to our Web site here on this page to notify you of these changes. Where the policies include changes to how we or our partners collect or use your personal information, or where new partners are permitted to collect certain information, that will also be reflected here and EU readers will be given an opportunity to opt-out of such collection and use.</p>
<p>If you are concerned about how your information is used, please periodically check back to this page.</p>
<p><strong>Can you opt out of our data collection?</strong></p>
<p>Our EU readers (readers visiting us from European Economic Area countries subject to the EU General Data Protection Regulation) will see a consent notice upon their first visit to this site. Our partners will only collect your information, in accordance with the terms above, if you affirmatively indicate consent to such collection and our use of cookies. If you give consent and would later like to revoke it, please contact us and we can work that out for you. If you would like to exercise any other rights under the GDPR, please contact us with your request.</p>
