<p>Our EU readers can opt of analytics tracking, as discussed below, though we hope you won&#8217;t as it only allows us to better understand our readers and serve your interests.</p>
<p><strong>What third-party advertisers do we use and what information do they collect?</strong></p>
<p>We use several third-party advertisers who serve either personalized or non-personalized ads. These advertisers typically use cookies or web beacons on our site which collect certain information and sends its back to these advertisers. Except for our EU readers who can opt out of personal information collection, as discussed below, we do not have access to or control over these cookies or the collection of information once we have given permission for them to set cookies for advertising. For more on that, we turn to the NAI disclaimer:</p>
<blockquote>We use third-party advertising companies to serve ads when you visit our Web site. These companies may use aggregated information (not including your name, address, email address or telephone number) about your visits to this and other Web sites in order to provide advertisements about goods and services of interest to you. If you would like more information about this practice and to know your choices about not having this information used by these companies, <a href="http://optout.networkadvertising.org/?c=1">click here</a>.</blockquote>
<p>As a result of your visit to our site and not opting out of advertising information collection, these advertising partners may collect information such as your domain type, your IP address, your ISP, your browser type, and your clickstream information. This information is used by them to serve personalized (e.g., based on a user&#8217;s location, prior site visits and/or visits to other sites on the internet) and/or non-personalized advertising. If you opt-out of this collection, you may still see advertisements where our advertiser partners are able to serve ads without the use of cookies or scripts to collect information, but they will be non-personalized and not based upon any of your information. These are the advertisers we currently use, either actively or from time-to-time, along with links to their respective privacy policies:</p>
