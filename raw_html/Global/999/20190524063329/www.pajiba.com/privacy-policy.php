<h1><strong><center>PAJIBA PRIVACY POLICY</center></strong></h1>
<p><strong>Last Updated:</strong> February 4, 2019.</p>
<p>Pajiba.com is a website owned and operated by Scathing Websites, LLL (&#8220;<strong>Pajiba</strong>&#8221;, &#8220;<strong>we</strong>&#8221; or &#8220;<strong>us</strong>&#8221;). In this privacy policy, we want to let you know:</p>
<p>&#8212;What information is collected by us;<br />
