<h3>Use of Cookies</h3>
<p>To improve and personalize your experience on our Site, we store cookies on your computer. A cookie is a piece of text stored on a user's computer by their web browser. Cookies may be used for authentication, storing site preferences, as an identifier for server-based session, etc. Cookies also allow us to collect Non-Personally Identifiable Information from you, such as which pages you visited and what links you clicked on.</p>
<p>SIU believes that its use of permanent cookies adds value to users not otherwise available. Specifically, SIU believes that all of the types of information collected, and SIU's use of that information, adds user value. For example, using cookies in this manner allows SIU to provide target content to users of our site.</p>
