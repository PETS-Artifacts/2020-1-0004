<p>We at Phone Scoop take your privacy very seriously. We believe that electronic privacy is crucial for the ongoing success of the Internet. Because we gather certain types of information about our users, we want to help you understand the terms and conditions surrounding the collection and use of that information. This privacy policy discloses the types of information we gather, how we use it, and how to correct or change it.</p>

<p>On occasion, Phone Scoop will make available to potential advertisers and business partners aggregated results from traffic and demographic information collected from its readership base via the phonescoop.com site. Phone Scoop will only provide traffic and demographic information on an aggregated basis and does not disclose individual reader information.</p>

<p>To ensure your absolute privacy, personal and demographic data are kept separate in our systems, so that demographic data can never be linked back to names or other identifying information. Furthermore, submission of demographic information is always optional.</p>

<h2>E-mail</h2>

<p>Any/all e-mail addresses sent to us as the result of registration or correspondence will not be collected or used for the purposes of reselling to a third party and will never be added to any bulk e-mailing list.</p>

<p>We can also provide complete assurance that your email address will never intentionally be displayed on the web site or revealed publicly in any way.</p>

<p>We collect and use e-mail addresses for the following purposes:</p>

<ul>
	<li>Sending weekly e-mail newsletters, if we offer such a service, and you explicitly opt in to receive them.</li>
	<li>Sending automated notifications that you explicitly request. For example, you can opt to receive e-mail notification when someone responds to a message you have posted in the forums.</li>
	<li>Relaying messages sent from other users who wish to contact you directly, IF, and only if, you have explicitly indicated that you wish to receive such messages. Receiving such messages will not reveal your email address to the sending party, unless you choose to reply. If you choose to initiate such a message to another user, your e-mail address will be revealed to the recipient privately as the reply-to address.</li>
	<li>Sending administrative notices regarding the site and/or your account. We will only send such notices when absolutely required, and action is required on your part. We will never send unsolicited promotional messages of any kind.</li>
</ul>

<h2>Access</h2>

<p>If you wish to correct/add/remove the personal information submitted to Phone Scoop, please log in using your account, and use the "<span class="update-2018-05-24">Account</span>" link, found in the left-hand navigation bar on every page. Or, you may contact us directly using the <a href="/site/contact.php?c=20">contact form</a>.</p>

<h2>Cookies</h2>

<p>Your Internet browser has a feature called "cookies", which allows phonescoop.com to store small bits of information on your computer, which are then sent back to phonescoop.com on every subsequent visit. Cookies allow you to register, log in, and maintain preferences and settings.</p>

<p>However, cookies tell us nothing about who you are, unless you specifically give us personally identifiable information. You do not need to have cookies turned on to visit most of our site, although active participation in certain areas of our site may require cookies. In addition, you may elect to disable cookies by selecting certain options on your browser.</p>

<p>The use of advertising cookies sent by third-party servers is standard in the Internet industry. In addition to phonescoop.com cookies, you may also receive cookies from our advertisers and affiliate partners. Phone Scoop cannot and does not control these cookies.</p>

<p>We use third-party advertising companies to serve ads when you visit our site. <span class="update-2018-05-24">We work directly with the ad networks Ziff Davis and Google. They, in turn, each have multiple partners that they use to select relevant ads, deliver them, and measure success.</span> These companies may use information (generally, not including your name, address, e-mail address or telephone number) about your visits to this web site and to other web sites in order to provide advertisements about goods and services of interest to you. These companies may employ cookies and clear GIFs to measure advertising effectiveness. Any information that these third parties collect via cookies and clear GIFs is generally not personally identifiable (unless, for example, you provide personally identifiable information to them through an ad or e-mail message). We encourage you to read these businesses' privacy policies if you should have any concerns about how they will care for your personal information. If you would like more information about this practice and to know your choices about not having this information used by these companies, see the <a href="http://www.networkadvertising.org/managing/opt_out.asp">Network Advertising Initiative's consumer web site</a>.</p>

<h2>Traffic Data</h2>

<p>Each time a visitor comes to the Phone Scoop site, information is collected to improve the overall quality of the site. Phone Scoop collects the visitor's domain name/IP Address (e.g., whether the user is logged on from netscape.com or yale.edu or 255.255.255.0), referral data (e.g., we record the address of the last URL a user visited prior to clicking through to a Phone Scoop site), and browser/platform type (e.g., a Netscape browser on a Macintosh platform).</p>

<p><span class="update-2018-05-24">This traffic data is collected and stored on both Phone Scoop and Google servers. We use Google Analytics to determine general information about how many people are using our site and how they navigate the site. This data is strictly aggregated, not personal. The only analytics data shared with third parties is the number of people that visit our site. The traffic data on our servers is log data that is retained strictly for internal audit purposes; it is not shared.</span></p>

<p>In addition, Phone Scoop's advertisement delivery system collects similar aggregate information, for  ad rotation control, accounting, and reporting.</p>

<p>Phone Scoop also counts, tracks, and aggregates each visitor's activity into our analysis of general traffic flows at phonescoop.com. Phone Scoop may share this information, on an aggregated basis only, with potential advertisers and business partners. Phone Scoop will not disclose your individual identity or personal data. When Phone Scoop does present aggregated information to potential advertisers and business partners, no one will be able to identify you or contact you.</p>

<p>If you have any questions or concerns about our privacy policy or practices, please do not hesitate to contact us using the <a href="/site/contact.php?c=21">contact form</a>.</p>

