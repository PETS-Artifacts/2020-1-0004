			<h1>Internet Privacy Policy</h1>
            <p>Thank you for visiting the Florida Atlantic University website. Our policy is to collect no personal information about you when you visit our website unless you choose to make such information available to us.</p>
            <p>When you visit our website, our Web server automatically recognizes only the Internet domain and IP address from which you accessed our website. This information does not result in the identification of your personal e-mail address or other personal information. In addition, we gather information regarding the volume and timing of access to our website by collecting information on the date, time and website pages accessed by visitors to the website. We do this so that we can improve the content of our website. Again, only aggregate information is collected, and individual visitors' personal information is not identified.</p>
            <p>As an agency of the State of Florida, information shared with us is subject to disclosure under the Florida Public Records laws, unless specifically exempted. If you choose to share personal information with us, it may released if required by the Florida Public Records Laws or in accordance with our policies, rules or regulations and must be saved for a designated period of time to comply with Florida’s record retention policies. We will not sell that information to commercial entities. Most student information is kept confidential pursuant to the federal and state Buckley Amendments.</p>
            <p>Please be advised that under Florida law, e-mail addresses are public records. If you do not want your e-mail address released in response to a public records request, do not send us electronic mail. Instead, contact us by phone or in writing.</p>
            <p>If you have other questions about our privacy policies, or have ideas about improving our policies, please feel free to e-mail us at <a href="mailto:fauweb@fau.edu">fauweb@fau.edu</a> or you can reach us by telephone at 561-297-3999.</p>
            <p>For more information regarding the privacy and protection of medical information and records at FAU, please visit: <a href="http://www.fau.edu/hipaa/" target="_blank">http://www.fau.edu/hipaa</a>
            </p>
