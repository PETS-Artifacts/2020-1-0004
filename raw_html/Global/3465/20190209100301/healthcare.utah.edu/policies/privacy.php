<h3>Introduction</h3>
<p>Academic freedom, the decentralized nature of the World Wide Web, and the diverse purposes served by University of Utah Web sites require that as much freedom as possible be granted to those creating and maintaining Web sites. We support and encourage freedom of expression and an open environment for the pursuit of knowledge; however, the contents of all web pages under University jurisdiction must comply with local, state and federal laws as well as University policy.</p>
<h3>Personal Information</h3>
<p>You do not have to disclose any information to visit any University site. However, while browsing some sites, you may find your needs can be better accessed by disclosing some information. The information you provide will not be sold, traded, or leased to any other organization outside of the University, without your prior written consent.</p>
<p>We may use demographic information that does not personally identify you and/or information you voluntarily provide, to improve the features of the site or for general statistical purposes.</p>
<h3>Information Automatically Collected and Stored</h3>
<p>As with any site you visit on the Internet, University Health Care does collect certain system information from its users. The University's site automatically stores the following information during your visit:</p>
<ul>
<li>The Internet location (IP address)</li>
<li>Which pages were last visited</li>
<li>The date and time of the visit</li>
<li>The software used to visit our site</li>
</ul>
<p>The information collected is not associated with your name or any other information you voluntarily provide. This information is stored and used for statistical purposes that allows us to improve the quality of this site, and to better fulfill the needs of our users.</p>
<h3>IP Addresses</h3>
<p>As stated above, we collect your computer&#8217;s IP address during your visit. An IP address is the number automatically assigned to your computer that allows computers and servers to recognize and communicate with each other. We use IP addresses to determine the geographic characteristics of our users and to allow internal users to view internal pages. Analyzing geographical information allows us to make this site more accessible to our users.</p>
<h3>Cookies</h3>
<p>Cookies are text files that a Web site transfers to a user&#8217;s hard disk to record certain information about your visit. Some University sites place cookies in the browser files of your computer to provide you with tailored health care information. You can chose not to download cookies or disable cookies.</p>
<h3>Web Analytics</h3>
<p>This website uses Google Analytics, a web analytics service provided by Google, Inc. ("Google").</p>
<p>Google Analytics uses "cookies", which are text files placed on your computer, to help the website analyze how users use the site. The information generated by the cookie about your use of the website (including your IP address) will be transmitted to and stored by Google on servers in the United States.</p>
<p>Google will use this information for the purpose of evaluating your use of the website, compiling reports on website activity for our website operators and providing other services relating to website activity and internet usage.</p>
<p>Google may also transfer this information to third parties where required to do so by law, or where such third parties process the information on Google's behalf. Google will not associate your IP address with any other data held by Google.</p>
<p>You may refuse the use of cookies by selecting the appropriate settings on your browser, however please note that if you do this you may not be able to use the full functionality of this website. By using this website, you consent to the processing of data about you by Google in the manner and for the purposes set out above. For more information on Google privacy practices, visit <a href="http://www.google.com/privacypolicy.html" target="_blank">http://www.google.com/privacypolicy.html</a><a href="http://www.google.com/privacypolicy.html"></a></p>
<p>This site uses Inspectlet to record some visitors' website experience. We use this data to improve our website and make it easier to use. We do not collect any personal information with this software. Data collected by inspectlet adheres to its <a href="http://www.inspectlet.com/legal#privacy" target="_blank">privacy policy</a>.&#160;</p>
