 <p>Because we gather certain types of information            about our visitors, we feel you should fully understand our policy and            the terms and conditions surrounding the capture and use of that information.            This            privacy statement discloses what information we gather and how we use            it.</p>
    <h2>Overview</h2>
    <p>Animals Australia  gathers two types of information            about visitors:</p>
    <ul><li><strong>Information that visitors provide through              optional, voluntary submissions.</strong> These are voluntary submissions              to receive our electronic newsletters,  to email a friend, and from participation in polls              and surveys.</li>
      <li><strong>Information Animals Australia gathers through              aggregated tracking information derived mainly by tallying page views              throughout our sites:</strong> this information allows us to better tailor              our content to readers' needs.  Animals Australia does not divulge any information              about an individual visitor to third parties for marketing purposes. </li>
      </ul><h2>What Information is Gathered? </h2>
    <p><strong>Optional Voluntary Information</strong></p>
    <p>We offer the following free services,            which require some type of voluntary submission of personal information            by visitors:</p>
    <ol><li><strong> Electronic newsletters policy (e-updates)</strong><br />
        We  offer a free electronic newsletter            to visitors. Animals Australia gathers the email addresses and optional contact details of visitors who voluntarily            subscribe. Visitors may remove themselves from this mailing list by following            the link provided in every newsletter.</li>
      <li><strong>"E-mail this to a friend" policy</strong><br />
        Our site visitors can choose to electronically            forward a link, page, or documents to someone else by clicking "e-mail            this to a friend". The visitor must provide their email address, as well            as that of the recipient. This information is used only in the case            of transmission errors and, of course, to let the recipient know who            sent the email. The information is not used for any other purpose.</li>
      <li><strong>Contact Forms</strong><br />
        We provide forms throughout this website for purposes such as contacting Animals Australia, requesting Action Packs and other purposes. All information that is volunatrily submitted in these forms will be recorded and used for the stated purpose of that form (such as responding to a request for information, action pack, etc.). These transactions are recorded by Animals Australia for record keeping purposes.</li>
