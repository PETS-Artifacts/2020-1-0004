    Campaign tools on our sites enable visitors to engage in fast and effective online activism. Online activism including petitions and advocacy campaigns may require certain information to be disclosed to petition or advocacy campaign targets for the purpose of lobbying or advocacy as indicated on all pages that include this feature. Animals Australia provides guidance to visitors on how to communicate for the purpose of lobbying and online activism. We are however, not responsible for the views or opinions made in any communication addressed to the intended target of a campaign.  Such views and opinions are solely those of the author and do not necessarily represent the views or policies of Animals Australia, its officers or employees. </li>
    </ol><p><strong>Usage Tracking</strong></p>
    <p>Animals Australia  tracks visitor traffic patterns            throughout all of our sites. However, we do not correlate this information            with data about individual visitors. Animals Australia does break down overall usage            statistics according to a visitor's domain name, browser type, and MIME            type by reading this information from the browser string (information            contained in every visitor's browser).</p>
    <p><strong>Cookies</strong></p>
    <p>We may place a text file called a "cookie" in            the browser files of your computer. The cookie itself does not contain            Personal Information although it will enable us to relate your use            of this site to information that you have specifically and knowingly            provided. But the only personal information a cookie can contain is            information you supply yourself. A cookie can't read data off your            hard disk or read cookie files created by other sites.</p>
    <p>You can refuse cookies by turning them            off in your browser. If you've set your browser to warn you before            accepting cookies, you will receive the warning message with each cookie.            You do not need to have cookies turned on to use this site.</p>
    <h2>Security</h2>
    <p>Animals Australia takes all reasonable efforts to protect the security of information obtained through our network of websites. Your personal information will never be disclosed to a third party for marketing purposes.</p><p>Occasionally Animals Australia will partner with colleague organisations to run joint campaign initiatives. In these rare cases, visitors may have the opportunity to join an e-mail list of our campaign partners on a jointly run website. Joint opt in preferences will always be clearly labelled on-screen. Please note that opting in to partner lists is never mandatory, and that lists run by partner organisations are governed by the privacy policy of the respective organisation.</p>
    <p>Please note that posing as a fraudulent identity or supplying information on behalf of others without their prior consent is in breach of the terms of use of this website.</p>
    <h2>Opt-In / Opt-Out Policy </h2>
    <p>Our mailing list is a strict <em>opt-in service</em>, meaning, you must specifically request to be included on the list. You will not be added to the mailing list without your consent. Visitors can <a href="http://animalsaustralia.org/e-updates.php">subscribe to the mailing list</a> manually, or by opting in via one of our website contact forms (see above). </p>
