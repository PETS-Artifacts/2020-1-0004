<h1 class="realtitle">Privacy policy</h1>



<div class="maincontent">

<div class="newhr"><img src="/images/hrshadow.jpg" alt="shadow" /></div>


<div id='startedreading'></div><p>Animals Australia is committed to protecting your privacy. As we gather certain types of personal and non-personal data we want to ensure you fully understand our policy and the conditions surrounding the capture and use of that information. We aim to provide you with clear and simple information about:</p>
<ul>
<li>why we collect certain information </li>
<li>how we protect your personal information </li>
<li>how you can contact us to find out more about your personal information </li>
<li>what to do if you have concerns about your privacy </li>
</ul>
<p>Animals Australia is compliant with the <em>Privacy Act 1988</em> (Cth) including the Notifiable Data Breach Scheme, and the EU General Data Protection Regulation for EU residents. </p>
<h2>Security </h2>
<p>Animals Australia is a data controller, which means we determine why and how we process personal data. We understand how important it is to keep your personal information safe and implement appropriate technical and organisational measures. We do not sell, rent or divulge any information about an individual to third parties for marketing purposes. </p>
<p>We are compliant with the Payment Card Industry Data Security Standard (PCI DSS), which is a set of enforceable security standards designed to ensure that organisations that accept, process, store or transmit credit card information maintain a secure environment.</p>
<h2>The Australian <em>Privacy Act 1988</em></h2>
<p>The <em>Privacy Act 1998</em> (Cth) includes 13 legally binding principles which set the basic standard for privacy protection at the Federal level. The APPs require us to take reasonable steps to make individuals aware that we are collecting personal information about them and why it's being collected. We must comply with restrictions on how personal information can be used and to whom it can be disclosed. Individuals have the right to access the information we hold about them and to have that information corrected or modified.</p>
<h2>Information we collect</h2>
<ul>
<li><strong>Information you provide to us: </strong>these are optional, voluntary submissions including signing up to receive our electronic newsletters, participation in polls, pledges, actions, and surveys, regular or one off donations and becoming a member, this may be through the website, phone, mail or email. </li>
<li><strong>Information Animals Australia gathers: </strong>through aggregated tracking information derived mainly by tallying page views throughout our websites, this information allows us to better tailor our website content to readers' needs. </li>
</ul>
<h2>Personal data</h2>
<p>Personal data is any information relating to an identified or identifiable person e.g. name, address, email etc. Personal data is collected by Animals Australia when supporters and members voluntarily submit this information. Personal data can be submitted when signing up to become a member, a donor, or when taking action through the website.</p>
<h2>How we use your personal data </h2>
<p>All information we collect is used to personalise and improve your experience when communicating with Animals Australia, either directly or through the website. This includes customised or localised content, recommendations and features. We contact you via email, mail, phone or SMS to communicate actions to support our work, such as writing to MPs and signing petitions, attending rallies and other campaign related actions which will help us carry out our work for animals. </p>
<p>If you are a regular donor to Animals Australia, we may contact you regarding your automated donations. We undertake analysis of our supporter base to better understand our donors and audiences, to help us become more effective in our work and organisational development. We process all donations through secure payment gateways and never store full credit card information on file.</p>
<h2>How we store your personal data </h2>
<p>All personal data is stored on secure servers. Any data received via email, phone, SMS, mail or through the website is collated either automatically or by trained staff. Any physical copies of data are disposed of with an accredited third party, via a locked, secure waste disposal service. Any personally identifiable information you elect to make publicly available on our website or social media channels will be available to others. </p>
<h2>Data retention, modification and deletion </h2>
<p>We keep your personal data for as long as your membership is active or as needed to provide you services, such as maintaining your membership or sending updates on our work. We will retain and use your information as necessary to comply with our legal, financial and operational requirements.</p>
<p>You can request a copy of your record by <a href="https://www.animalsaustralia.org/about/contact.php">contacting us</a> and asking for a Data Subject Access Request Form, which will outline the process for obtaining a copy of your personal data. Contact the Animals Australia office via email, phone or mail (details below) to update or remove any personal data.</p>
<h2>EU General Data Protection Regulation (EU GDPR)</h2>
<p>This section relates to EU supporters specifically due to compliance with the EU GDPR. Animals Australia complies with the GDPR framework regarding the collection, use and storage of personal data from EU member countries. Our legal basis for processing your personal information will typically be because data collected by us has been given consensually for a specific purpose, or because processing is necessary for pursuing a legitimate interest of the organisation. </p>
<p>Legitimate interests include but are not limited to collecting email addresses for communication purposes, financial information for payment purposes or placing cookies to remember passwords and user preferences, etc. </p>
<p>All EU members and subscribers have a number of rights under the EU GDPR regulations. This includes:</p>
<ul>
<li>The right to be informed: this privacy policy must be written in clear and plain language and be easily accessible. </li>
<li>The right of access: <ul>
<li>you can request a copy of your record by <a href="https://www.animalsaustralia.org/about/contact.php">contacting us</a> and asking for a Data Subject Access Request Form, which will outline the process for requesting a copy of your personal data. </li>
</ul>
</li><li>The right to rectification: contact the Animals Australia office via email, phone or mail (details below) to update any personal data </li>
<li>The right to be forgotten: contact the Animals Australia office via email or mail (details below) to request your personal data be removed entirely from our systems, note this must be done in writing to confirm your identity </li>
<li>The right to restrict processing: you can block or restrict the processing of your personal data </li>
<li>The right to object: let us know if you're receiving any material that you're not happy about and we'll do our best to ensure you don't get it again </li>
<li>Right to Data Portability: you can request a copy of your personal data in electronic format and transmit that personal data for use in another party's service </li>
<li>Right not to be subject to Automated Decision-making: to not be subject to a decision based solely on automated decision making, including profiling, where the decision would have a legal effect on you or produce a similarly significant effect </li>
</ul>
<h2>Data Breach</h2>
<p> Animals Australia have obligations under the <em>Privacy Act 1988</em> (Cth) to put in place security safeguards and take steps to protect the personal information we hold. However, no system is ever completely secure. Under the Notifiable Data Breach Scheme (as part of the <em>Privacy Act 1988</em>), we have obligations to notify the Office of the Australian Information Commissioner (OAIC) and affected individuals when an eligible data breach occurs. Animals Australia is compliant with the scheme. The process for responding to data breaches is outlined in our Data Breach Response Plan. This pertains to all supporters regardless of location. </p>
<h2>Our Websites</h2>
<p>Our websites are owned and operated by Animals Australia Federation. We offer the following free services, which require some type of voluntary submission of personal information by visitors. Please note that posing as a fraudulent identity or supplying information on behalf of others without their prior consent is in breach of the terms of use of the Animals Australia website:</p>
<ul>
<li><strong>Electronic newsletters policy (e-updates)</strong><br />
We offer several free electronic newsletters to visitors. Animals Australia gathers the email addresses and optional contact details of visitors who voluntarily subscribe. Visitors may remove themselves from this mailing list by following the link provided in every newsletter. </li>
<li><strong>Contact Forms</strong><br />
We provide forms throughout this website for purposes such as contacting Animals Australia, requesting Action Packs and other purposes. All information that is voluntarily submitted in these forms will be recorded and used for the stated purpose of that form (such as responding to a request for information, action pack, etc.). These interactions are recorded by Animals Australia for record keeping purposes. </li>
<li><strong>Web-enabled digital actions</strong><br />
Campaign tools on our sites enable visitors to engage in fast and effective online activism. Online activism including petitions, pledges, and other actions may require certain information to be disclosed to campaign targets for the purpose of lobbying or advocacy. Any personally identifiable contact information you supply for the purposes of these actions may be disclosed to the stated campaign target (such as your full name, e-mail address, postcode, etc.). If you do not wish for these details to be disclosed to the campaign target, you are encouraged not to engage in the online action. Animals Australia provides form letters or guidance to visitors on how to communicate for the purpose of lobbying and online activism. We are however, not responsible for the views or opinions made in any personalised communication addressed to the intended target of a campaign. Such views and opinions are solely those of the author and do not necessarily represent the views or policies of Animals Australia, its officers or employees. </li>
</ul>
<h2>Usage Tracking</h2>
<p>Animals Australia tracks visitor traffic patterns throughout all our sites. However, we do not correlate this information with data about individual visitors. Animals Australia does break down overall usage statistics according to a visitor's IP address, browser type, and MIME type by reading this information from the browser string (information contained in every visitor's browser). </p>
<h2>Cookies </h2>
<p>We may place a text file called a "cookie" in the browser files of your computer. The cookie will enable us to relate your use of this site to information that you have specifically and knowingly provided. The only personal information a cookie can contain is information you supply yourself. A cookie can't read data off your hard disk or read cookie files created by other sites. </p>
<p>The Animals Australia sites use cookies to help personalise your online experience by storing your preferences and other information on your computer. Cookies also collect information about the actions you take while on the website e.g. pages you view or links you click. They help us to build a profile of how our readers use the website, so we can ensure visitors have an easy and meaningful experience when engaging with our content. </p>
<p>You can refuse cookies by turning them off in your browser. If you've set your browser to warn you before accepting cookies, you will receive the warning message with each cookie. You do not need to have cookies turned on to use this site. </p>
<h2>Partnerships</h2>
<p>Occasionally Animals Australia will partner with colleague organisations to run joint campaign initiatives. In these rare cases, visitors may have the opportunity to join an e-mail list of our campaign partners on a jointly run website. Joint opt-in preferences will always be clearly labelled on-screen. Please note that opting in to partner lists is never mandatory, and that lists run by partner organisations are governed by the privacy policy of the respective organisation.
</p><h2>Opt-In and Opt-Out</h2>
<p> If you sign an Animals Australia pledge, petition, or other online action, then on certain pages that clearly list recent signatories, your first name and country may appear on our website for a brief period of time. If you are based in the EU, you can opt in to receive further correspondence from us regarding our work during this interaction. If you are based outside the EU, you can opt out of receiving further correspondence from us regarding our work during this interaction. Visitors can <a href="http://animalsaustralia.org/e-updates.php">subscribe</a> or <a href="https://www.animalsaustralia.org/unsubscribe.php">unsubscribe</a> to the mailing lists manually. Links to opt-out of each mailing list (Animals Australia and VegKit) are included at the bottom of every email broadcast that we send. You can contact our office at any time to <a href="https://www.animalsaustralia.org/features/sms-alerts.php">opt in</a> or <a href="https://www.animalsaustralia.org/sms-unsubscribe.php">opt out of SMS</a> and <a href="https://www.animalsaustralia.org/unsubscribe.php">mail communication</a>.</p>
<h2>Your Consent</h2>
<p>By using this site, you consent to the collection and use of this information by Animals Australia. If we decide to change our privacy policy, we will post those changes on this page so that you are always aware of what information we collect, how we use it, and under what circumstances we disclose it.</p>
